<p style="text-align: center;"><strong>Case Manager</strong></p>
<p>&nbsp;<strong>REPORTING RELATIONSHIP:</strong></p>
<p>&nbsp;</p>
<p>Supervised by:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Director</p>
<p>Positions Supervised: &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Hospice Staff</p>
<p>Interrelationships: &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Patients, family, IDG and other health care team members</p>
<p>&nbsp;</p>
<p><strong>JOB SUMMARY:</strong></p>
<p>Responsible as a member of the interdisciplinary and clinical management teams to the Director for the implementation, delivery, coordination and supervision of Hospice services delivered by respective team consistent with Hospice policies and procedures.</p>
<p>&nbsp;</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>Educational/Degree:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Graduate from an accredited school of nursing</p>
<p>&nbsp;</p>
<p>Training/Licensure:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Currently licensed as a registered nurse to practice in the state.</p>
<p>&nbsp;</p>
<p>Knowledge/Skills/Ability:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ability to work independently, make accurate, and at times, quick judgments. Ability to supervise others appropriately. Ability to respond appropriately to crisis outside of a hospital setting. Acceptance of and adaptability to different social, racial, cultural and religious modes.</p>
<p>&nbsp;</p>
<p>Experience:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Minimum 2 years of experience as a registered nurse, preferred.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Active patient contact within past three years, preferred.</p>
<p>&nbsp;</p>
<p><strong>JOB FACTORS:</strong></p>
<p>&nbsp;</p>
<p><strong><u>Physical Requirements:</u></strong></p>
<p>Requires considerable physical effort most of the day including kneeling, squatting, reaching, twisting, climbing, walking, exposure to temperature and humidity changes and maximal assist in lifting and/or transferring of a 100 pound patient. Must possess sight/hearing senses or use appropriate adaptive devices that will enable senses to function at a level required to meet the essential duties of the position. Must provide evidence of annual TB test and other state-required tests or examinations.</p>
<p>&nbsp;</p>
<p><strong><u>Mental Requirements:</u></strong></p>
<p>Must be able to work independently, make judgments based on assessments and data available and act accordingly. Must be flexible, innovative and possess good interpersonal skills. Must be able to cope with mental and emotional stress and demonstrate emotional stability.</p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>Working Conditions: </u></strong>Be able to tolerate exposure to elements including, but not limited to, odors, blood, body fluids and excrements, adverse environmental conditions and hazardous materials.</p>
<p><strong><em>Job Description &ndash; Case Manager &hellip;.continued</em></strong></p>
<p>&nbsp;</p>
<p><strong><u>Transportation:</u></strong></p>
<p>Must have a current valid driver&rsquo;s license, auto liability insurance and reliable transportation.</p>
<p>&nbsp;</p>
<p><strong>Essential Functions:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Supervise the planning, coordination and delivery of the interdisciplinary group interventions.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Assign patient/family to appropriate staff member(s).</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Participate and provide leadership in the team for regular interdisciplinary group meetings; the development, review and revision of the patient/family plan of care; facilitation of problem solving sessions; and delivery of team support.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Implement and monitor compliance with policies and procedures.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Serve as a member of the management team in order to address problems associated with the delivery of services and staff development needs, to put forth suggestions for appropriate additions/changes in policies and procedures and to give peer input to enhance and improve the management function.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Supervise the maintenance of medical records assuring accuracy, completeness and compliance with licensing regulations, certification standards, legal and ethical standards.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Provide supervision of support and clerical staff in cooperation with other members of the management team.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Oversee clinical supervision of nursing personnel, Hospice Aides and Homemakers.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Conduct appropriate supervisory visits which allow for clinical observation of performance of assigned team members.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Provide information for inclusion in written performance evaluations for all staff assigned to team in cooperation with appropriate members of the management team.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Provide leadership and counsel to nursing personnel to measurably enhance staff development.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Identify need for staff education and assist in the development of inservice programs for interdisciplinary group.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Assist in education/training programs.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Participate in orientation of new personnel as appropriate to team responsibilities.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Perform specific assignments as directed by the Director.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Regularly assesses and reassesses the nursing needs of the Hospice patient.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Provides Hospice nursing services, treatments and preventive procedures.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Teaches, supervises and counsels the Hospice patient and family members about providing care for the patient.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Performs specific nursing procedures as needed (e.g., treatments, management of symptoms) following doctor&rsquo;s orders.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Observes confidentiality and safeguards all patient-related information in compliance with HIPAA regulations.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Always communicates to the Director if unable to meet a patient&rsquo;s need or perform a procedure.</li>
</ol>
<ol>
<li>Participates in on-call system and is responsible for providing on-call coverage when unavailable for assigned duties.</li>
</ol>
<ol>
<li>Maintain skills and knowledge.</li>
</ol>
<ol>
<li>Participates in QAPI program.</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>I have read the above job description and understand the duties and responsibilities associated with the position. I can perform the essential functions of this position without specific accommodations.</p>
