<p style="text-align: center;"><strong>Biller</strong></p>
<p><u>&nbsp;</u></p>
<p><strong>JOB SUMMARY:</strong></p>
<p>&nbsp;</p>
<p>The Biller reports to the Administrator and possesses knowledge, training and experience in insurance and billing.</p>
<p>&nbsp;</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>&nbsp;</p>
<ol>
<li>High school graduate or equivalent (GED).</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Preferably one year&rsquo;s experience in healthcare billing and Hospice.</li>
</ol>
<p>&nbsp;</p>
<p><strong>KNOWLEDGE AND ABILITY:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Knowledge of Medicare, Medicaid and third party reimbursement requirements.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Ability to provide knowledge and skills to billing.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Knowledge of Hospice policies and procedures.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Ability to exercise independent judgment.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Ability to work with individuals.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Ability to deal effectively with high levels of stress.</li>
</ol>
<p>&nbsp;</p>
<p><strong>JOB DUTIES:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Verifies that all required patient information is present prior to preparing claims.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Utilizes appropriate forms for billing submission.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Submits timely billing of all patient accounts including Medicare, Medicaid, third party payers and patient co-pays.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Knowledgeable of intermediary billing policies and requirements.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Promptly follows up with each denial claim.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Submits required documentation for each denied claim within established time frame.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Assists in implementation of policies and procedures.</li>
</ol>
<p>&nbsp;</p>
<p style="text-align: center;">&nbsp;</p>
<p><strong><em>Job Description &ndash; Biller&hellip;.continued</em></strong></p>
<p>&nbsp;</p>
<ol start="8">
<li>Displays a willingness to support policies and procedures and uses appropriate channels for changes of such policies.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Serves as a role model for all colleagues by setting an example of high standards in dress, conduct, cooperation and job performance.</li>
</ol>
<p>&nbsp;</p>
<ol start="10">
<li>Observes confidentiality and safeguards all patient related information.</li>
</ol>
<p><strong>&nbsp;</strong></p>
<ol start="11">
<li>Accepts responsibility for regular attendance and punctuality.</li>
</ol>
<p>&nbsp;</p>
<ol start="12">
<li>Serves as a resource person to all employees.</li>
</ol>
<p>&nbsp;</p>
<ol start="13">
<li>Responsible for answering telephone and forwarding call to appropriate personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="14">
<li>File documentation/paperwork in appropriate chart.</li>
</ol>
<p>&nbsp;</p>
<p><strong>LIFTING REQUIREMENTS:</strong></p>
<p>&nbsp;</p>
<p>Moderate lifting may be required.</p>
<p>&nbsp;</p>
<p><strong>JOB RELATIONSHIPS:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Supervised by: Director</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Workers Supervised: N/A</li>
</ol>
<p>&nbsp;</p>
<p><strong>RISK EXPOSURE to BLOODBORNE PATHOGENS:</strong></p>
<p>&nbsp;</p>
<p>Low risk.</p>
