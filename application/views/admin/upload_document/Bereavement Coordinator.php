<p><strong><em>Job Description &ndash; Bereavement Coordinator&hellip;.continued</em></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>Working Conditions:</u></strong></p>
<p>Be able to tolerate exposure to elements including, but not limited to, odors, blood, body fluids and excrements, adverse environmental conditions and hazardous materials.</p>
<p><strong>&nbsp;</strong></p>
<p><strong><u>Transportation:</u></strong></p>
<p>Must have a current valid driver&rsquo;s license, auto liability insurance and reliable transportation.</p>
<p>&nbsp;</p>
<p><strong>Essential Functions:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Manage the bereavement services program utilizing professional staff and volunteers.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Oversee adequacy and appropriateness of bereavement programs for patient and family members.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Develop new bereavement programs and services as needed.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Develop educational programs and materials for patients/families, staff and the community regarding loss, grief and coping with bereavement.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Assist Hospice in educational training program.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Design materials for distribution to families eligible for and/or receiving Hospice bereavement services.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Plan, implement and supervise bereavement group events.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Supervise support staff involved in bereavement program.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Oversee bereavement follow-up by patient care staff and volunteers.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Oversee the volunteer component of the bereavement services.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Conduct the bereavement section of the IDG conference.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Participate in the maintenance of the bereavement component of the community and staff resource libraries.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Participate in the orientation and training of new employees and volunteers working in the Hospice program.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Prepare reports as requested by management.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Attends IDG conferences.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Works with IDG concept of patient care.</li>
</ol>
