<p style="text-align: center;"><strong>Bereavement Coordinator</strong></p>
<p>&nbsp;</p>
<p><strong>REPORTING RELATIONSHIP:</strong></p>
<p>&nbsp;</p>
<p>Supervised by:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Director/Manager of Patient Services</p>
<p>Positions Supervised: &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Bereavement Staff</p>
<p>Interrelationships: &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Patients, family, IDG and other health care team members</p>
<p>&nbsp;</p>
<p><strong>JOB SUMMARY:</strong></p>
<p>&nbsp;</p>
<p>To provide supportive services to help meet the needs of the terminally ill Hospice patient and family as needed. To provide assistance and understanding to the family in time of bereavement. To work as a member of the Hospice team in providing Hospice care. Responsible for developing, implementing and supervising the bereavement program and for the delivery of related services.</p>
<p>&nbsp;</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>&nbsp;</p>
<p>Educational/Degree:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Graduate from an accredited institution of social work, preferred.&nbsp;&nbsp;</p>
<p>&nbsp;</p>
<p>Knowledge/Skills/Ability:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ability to work independently, make accurate, and at times, quick judgments. Ability to supervise others appropriately. Ability to respond appropriately to crisis outside of a hospital setting. Acceptance of and adaptability to different social, racial, cultural and religious modes.</p>
<p>&nbsp;</p>
<p>Experience:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Minimum 2 years of experience in a related field, preferred.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Must have experience or education in grief or loss counseling.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Active patient contact within past three years, preferred.</p>
<p>&nbsp;</p>
<p><strong>JOB FACTORS:</strong></p>
<p>&nbsp;</p>
<p><strong><u>Physical Requirements:</u></strong></p>
<p>Requires minimal physical effort most of the day including kneeling, squatting, reaching, twisting, climbing, walking, exposure to temperature and humidity changes and minimal assist in lifting and/or transferring of a 20 pound patient. Must possess sight/hearing senses or use appropriate adaptive devices that will enable senses to function at a level required to meet the essential duties of the position. Must provide evidence of annual TB test and other state-required tests or examinations.</p>
<p>&nbsp;</p>
<p><strong><u>Mental Requirements:</u></strong></p>
<p>Must be able to work independently, make judgments based on assessments and data available and act accordingly. Must be flexible, innovative and possess good interpersonal skills. Must be able to cope with mental and emotional stress and demonstrate emotional stability.</p>
