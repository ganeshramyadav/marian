<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp; <em>Occupational Therapist</em></p>
<p>&nbsp;</p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Supervisor</em></p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<h3>Job Description Summary</h3>
<p>&nbsp;</p>
<p>The occupational therapist contracted through the Organization is responsible for the implementation of standards of care for occupational therapy services and for adherence to all conditions outlined in the Service Agreement.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<h3>Essential Job Functions/Responsibilities</h3>
<p>&nbsp;</p>
<ol>
<li>Evaluates patient&rsquo;s functional status (muscle function, endurance, visual coordination, written and verbal communication skills, self care ability, work capacity, etc.). Evaluates home environment for hazards or barriers to more independent living.&nbsp; Identifies equipment needs.&nbsp; Participates in the development of the total plan of care.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>For patients who plan to return to work, the occupational therapist may perform work capacity evaluation and may refer to specialized vocational training in accordance with Organization policy.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Develops treatment program and establishes goals for improved function. Communicates plan of care to referring physician and other Organization professionals.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>May teach new skills or retrain patient in once familiar daily activities that have been lost due to illness or injury, in accordance with Organization policy.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>May fabricate splints and instruct patient in the use of various types of adaptive equipment to improve function.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>May train patient in the use of prosthetic and/or orthotic devices.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Maintains appropriate clinical records, clinical notes, and reports to the physician any changes in the patient&rsquo;s condition. Submits these records in accordance with Organization policy.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Maintains contact/communication with other personnel involved in the patient&rsquo;s care to promote coordinated, efficient care.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Attends and contributes to inservices, case conferences, and other meetings as required by Organization policy to ensure coordinated and comprehensive plans of care for the patients of the Organization.</li>
<li>Identifies patient and family/caregiver needs for other home health services and refers <br /> as appropriate.</li>
</ol>
<p>&nbsp;</p>
<ol start="11">
<li>Instructs patient&rsquo;s family/caregiver and other Organization health care personnel in patient&rsquo;s treatment regime when indicated and appropriate.</li>
</ol>
<p>&nbsp;</p>
<ol start="12">
<li>Supervises Certified Occupational Therapy Assistants according to organization policy and state regulations.</li>
</ol>
<p>&nbsp;</p>
<ol start="13">
<li>When therapy is the only skilled service, instructs, supervises and evaluates home health aide care every two (2) weeks.</li>
</ol>
<p>&nbsp;</p>
<ol start="14">
<li>Participates in discharge planning for patient.</li>
</ol>
<p>&nbsp;</p>
<p>The above statements are only meant to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job related tasks other than those stated in this description.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<h3>Position Qualifications</h3>
<p>&nbsp;</p>
<ol>
<li>Graduate of an occupational therapy curriculum accredit&shy;ed jointly by the Committee on Allied Health Education and Accreditation of the American Medical Association and the American Occupational Therapy Association.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Registered with the National Registration Examination of the American Occupational Therapy Association.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Two years of clinical experience as an occupational therapist.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Demonstrates good verbal and written communication, and organization skills.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Possess and maintains current CPR Certification.</li>
</ol>
