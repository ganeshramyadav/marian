<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp; <em>Hospice Medical Social Worker</em></p>
<p>&nbsp;</p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Supervisor</em></p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<h3>Job Description Summary</h3>
<p>&nbsp;</p>
<p>The medical social worker is responsible for the implementation of standards of care for medical social work services.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<h3>Essential Job Functions/Responsibilities</h3>
<p>&nbsp;</p>
<ol>
<li>Assesses the psychosocial status of patients related to the patient&rsquo;s illness and environment and communicates findings to the registered nurse.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Carries out social evaluations and plans intervention based on evaluation findings.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Maintains clinical records on all patients referred to social work.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Provides information and referral services for Organization patients and families/caregivers regarding practical and environmental needs.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Provides information to patients or families/caregivers and community agencies.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Serves as liaison between patients or families/caregivers and community agencies.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Maintains collaborative relationships with Organization personnel to support patient care.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Maintains and develops contracts with public and private agencies as resources for patient and organization personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Participates in the development of the total plan of care and case conferences as required.</li>
</ol>
<p>&nbsp;</p>
<ol start="10">
<li>Assists physician and other team members in understanding significant social and emotional factors related to health problems.</li>
</ol>
<p>&nbsp;</p>
<ol start="11">
<li>Participates in discharge planning.</li>
</ol>
<p>&nbsp;</p>
<ol start="12">
<li>Other duties as delegated by the Clinical Director/Supervisor.</li>
</ol>
<p>&nbsp;</p>
<p>The above statements are only meant to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job related tasks other than those stated in this description.</p>
