<p><strong>JOB DESCRIPTION</strong></p>
<p><strong>Chaplain/Spiritual Counselor</strong></p>
<p>&nbsp;</p>
<p><strong>REPORTING RELATIONSHIP:</strong></p>
<p>&nbsp;</p>
<p>Supervised by:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Manager of Patient Services</p>
<p>Positions Supervised:&nbsp; N/A</p>
<p>Interrelationships:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Patients, family, IDG and other health care team members</p>
<p>&nbsp;</p>
<p><strong>JOB SUMMARY:</strong></p>
<p>&nbsp;</p>
<p>To provide spiritual and emotional support when needed to the Hospice patient/family and to members of the Hospice team. To provide assistance at the time of the patient&rsquo;s death and support of the family during the bereavement period.</p>
<p>&nbsp;</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>&nbsp;</p>
<p>Educational/Degree:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Graduate of an accredited seminary or school of theology or appropriate certification in hospital or pastoral ministry.</p>
<p>&nbsp;</p>
<p>Training/Licensure:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Certificate or degree in pastoral ministry.</p>
<p>&nbsp;</p>
<p>Knowledge/Skills/Ability:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ability to work independently, make accurate, and at times, quick judgments. Ability to respond appropriately to crisis outside of a hospital setting. Acceptance of and adaptability to different social, racial, cultural and religious modes.</p>
<p>&nbsp;</p>
<p>Experience:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Minimum 2 years of experience as a chaplain/spiritual counselor, preferred.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Active patient contact within past three years, preferred.</p>
<p>&nbsp;</p>
<p><strong>JOB FACTORS:</strong></p>
<p>&nbsp;</p>
<p><strong><u>Physical Requirements:</u></strong></p>
<p>Requires minimal physical effort most of the day including kneeling, squatting, reaching, twisting, climbing, walking, exposure to temperature and humidity changes and minimal assist in lifting and/or transferring of a 20 pound patient. Must possess sight/hearing senses or use appropriate adaptive devices that will enable senses to function at a level required to meet the essential duties of the position. Must provide evidence of annual TB test and other state-required tests or examinations.</p>
<p>&nbsp;</p>
<p><strong><u>Mental Requirements:</u></strong></p>
<p>Must be able to work independently, make judgments based on assessments and data available and act accordingly. Must be flexible, innovative and possess good interpersonal skills. Must be able to cope with mental and emotional stress and demonstrate emotional stability.</p>
<p><strong><u><br /> </u></strong></p>

<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>Working Conditions:</u></strong></p>
<p>Be able to tolerate exposure to elements including, but not limited to, odors, blood, body fluids and excrements, adverse environmental conditions and hazardous materials.</p>
<p><strong>&nbsp;</strong></p>
<p><strong><u>Transportation:</u></strong></p>
<p>Must have a current valid driver&rsquo;s license, auto liability insurance and reliable transportation.</p>
<p>&nbsp;</p>
<p><strong>Essential Functions:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Provide direct spiritual support and/or counsel to patients/families in keeping with patients&rsquo;/families&rsquo; beliefs.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Work with staff, clergy and community groups to enhance their sensitivity to the spiritual concerns of patients/families experiencing terminal illness and loss.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Participate in IDG conference by exploring and assessing the potential spiritual needs of patients/families and reporting on services as indicated.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Provide bereavement follow-up services as assigned.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Maintain proper records of visits to patients/families.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Make contact with clergy or appropriate representatives of patients/families as indicated.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Perform occasional liturgical assignments, e.g., memorial services with staff.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Conduct or make arrangements for funeral or memorial service when indicated.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Develop and maintain a resource group of clergy to whom specific aspects of spiritual care may be delegated.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Arrange for on-call availability of spiritual services.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Provide educational programs for community clergy, religious and lay representatives as resources allow.</li>
</ol>
<p>&nbsp;</p>
