<p><strong>JOB DESCRIPTION</strong></p>
<p><strong>Hospice Licensed Vocational Nurse (LVN)</strong></p>
<p>&nbsp;</p>
<p><u>JOB SUMMARY</u>:</p>
<p>&nbsp;</p>
<p>A qualified Licensed Practical/Vocational Nurse administers nursing care to terminally ill Hospice patients as needed. This is performed in accordance with physician orders and IDG plan of care under the direction and supervision of the Registered Nurse. Services are furnished in accordance with Hospice policies.</p>
<p>&nbsp;</p>
<p><u>QUALIFICATIONS</u>:</p>
<p>&nbsp;</p>
<ol>
<li>Graduate of a state approved school of practical (vocational) nursing and currently licensed in the state(s) in which practicing.</li>
<li>Minimum of one (1) year experience in nursing, preferred.</li>
<li>Acceptance of philosophy and goals of this Hospice.</li>
<li>Ability to exercise initiative and independent judgment.</li>
<li>Completion of a Hospice training program prior to providing care.</li>
</ol>
<p>&nbsp;</p>
<p><u>RESPONSIBILITIES</u>:</p>
<p>&nbsp;</p>
<ol>
<li>Understands and adheres to established policies and procedures.</li>
<li>Implements the IDG nursing care plan for each patient.</li>
<li>Provides nursing services, treatments and diagnostic and preventive procedures as assigned.</li>
<li>Observes signs and symptoms and reports to the physician and RN reactions to treatments, including drugs and changes in the patient's physical or emotional condition.</li>
<li>Teaches and counsels the patient and family/significant others regarding the nursing care needs and other related problems of the patient at home.</li>
<li>Evaluates with registered nurse the effectiveness of the LPN's/LVN&rsquo;s nursing service to the patient and family under the guidance of the registered nurse.</li>
<li>Maintains accurate and complete records of observations, treatments and care of patient.</li>
<li>Participates in medical record audit as assigned.</li>
<li>Attends staff meetings, IDG conferences and inservices as scheduled.</li>
<li>Takes on-call duty, nights, weekends and holidays as assigned.</li>
<li>Is responsible for:</li>
</ol>
<ul>
<li>Submitting any changes in schedule to Director or Manager of Patient Services on a daily basis.</li>
<li>Participating in IDG conferences to discuss the need for involvement of other members of the health team.</li>
</ul>
<ol start="12">
<li>Prepares clinical and progress notes.</li>
<li>Assists the physician and RN in performing specialized procedures.</li>
<li>Prepares equipment and materials for treatments.</li>
<li>Observes aseptic technique as required.</li>
<li>Assists the patient/family in learning appropriate self-care techniques.</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>

<p>&nbsp;</p>
<p><u>WORKING ENVIRONMENT</u>:</p>
<p>&nbsp;</p>
<p>Works indoors in Hospice office and patient homes and travels to/from patient homes.</p>
<p>&nbsp;</p>
<p><u>JOB RELATIONSHIPS</u>:</p>
<p>&nbsp;</p>
<ol>
<li>Supervised by: Director or Manager of Patient Services</li>
</ol>
<p>&nbsp;</p>
<p><u>RISK EXPOSURE</u>:</p>
<p>&nbsp;</p>
<p>High risk</p>
<p>&nbsp;</p>
<p><u>LIFTING REQUIREMENTS</u>:</p>
<p>&nbsp;</p>
<p>Ability to perform the following tasks if necessary:</p>
<ul>
<li>Ability to participate in physical activity.</li>
<li>Ability to work for extended period of time while standing and being involved in physical activity.</li>
<li>Heavy lifting.</li>
<li>Ability to do extensive bending, lifting and standing on a regular basis.</li>
</ul>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>I have read the above job description and fully understand the conditions set forth therein, and if employed as a Licensed Practical/Vocational Nurse, I will perform these duties to the best of my knowledge and ability.</p>
