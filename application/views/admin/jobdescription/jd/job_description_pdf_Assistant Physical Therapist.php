<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp; <em>Physical Therapy Assistant</em></p>
<p>&nbsp;</p>
<p><strong>Reports To:</strong> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Physical Therapist or Rehab Supervisor</em></p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<h3>Job Description Summary</h3>
<p>&nbsp;</p>
<p>The physical therapy assistant provides physical therapy services under the direction of the physical therapist and according to the physician's plan of care.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<h3>Essential Job Functions/Responsibilities</h3>
<p>&nbsp;</p>
<ol>
<li>Provides direct physical therapy according to directions of physical therapist and in accordance with the physician's plan of care.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Treats patient to relieve pain, develop or restore function, and maintain maximum performance.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Directs and aids patients in active and passive therapeutic exercises, muscle reeducation, gait and functional training, transfer activities and prosthetic training.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Utilizes physical agents, including, but not be limited to heat, cold, water, light, electricity, ultrasound and massage.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Observes and reports to the physical therapist and the Organization personnel the patient&rsquo;s reaction to treatment and any changes in patient&rsquo;s condition.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Instructs patients in care and use of wheelchairs, braces, crutches, canes, and prosthetic or orthotic devices.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Maintains necessary records; clinical notes and conference notes, which will be incorporated into the patient&rsquo;s clinical record in prescribed time frames.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Participates in Organization Home Care Team Members meetings and Organization Inservice Education Program.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Performs other duties as assigned.</li>
</ol>
