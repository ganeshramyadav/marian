<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Licensed Practical/Vocational Nurse</em></p>
<p><em>&nbsp;</em></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Supervisor/Nursing Supervisor</em></p>
<p>&nbsp;</p>
<h3><strong>Job Description Summary</strong></h3>
<p>&nbsp;</p>
<p>The Licensed Practical/Vocational Nurse is responsible for providing direct patient care under the supervision of a registered nurse.&nbsp; Responsibilities include following the plan of care, providing treatments, and working collaboratively with the members of the team to help meet positive patient care outcomes.</p>
<p><strong>&nbsp;</strong></p>
<h3><strong>Essential Job Functions/Responsibilities</strong></h3>
<p>&nbsp;</p>
<ol>
<li>Provides direct patient care as defined in State&rsquo;s Name Nurse Practice Act.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Implements plan of care initiated by the registered nurse.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Provides accurate and timely documentation consistent with the plan of care.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Assesses and provides patient and family/caregiver education and information pertinent to diagnosis and plan of care.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Participates in coordination of home health services, appropriately reporting the identified needs for other disciplines (HHA, OT, PT, MSW, ST, Dietician) to the registered nurse and/or Clinical Supervisor/Nursing Supervisor.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Uses equipment and supplies effectively and efficiently.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Participates in personal and professional growth and development.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Performs other duties as assigned by the registered nurse.</li>
</ol>
<p>&nbsp;</p>
<p>The above statements are intended to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job related tasks other than those stated in this description.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong><em>&nbsp;</em></strong><strong>Position Qualifications</strong></p>
<p>&nbsp;</p>
<ol>
<li>Graduate of an accredited practical nurse or vocational nursing program.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Has three years nursing experience and, effective January 1, 1998, shall have one year prior professional nursing experience in Community/home health or medical/surgical experience.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Currently licensed as a licensed practical nurse or licensed vocational nurse in the state.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Complies with accepted professional standards and practice.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Demonstrates good verbal and written communication, and organization skills.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Must be a licensed driver with an automobile that is insured in accordance with state and organization requirements and is in good working order.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Possesses and maintains current CPR certification.</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
