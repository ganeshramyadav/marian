<p><strong>JOB DESCRIPTION</strong></p>
<p><strong>Hospice Registered Nurse (RN)</strong></p>
<p>&nbsp;</p>
<p><strong>REPORTING RELATIONSHIP:</strong></p>
<p>&nbsp;</p>
<p>Supervised by:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Case Manager of Patient Services/Director</p>
<p>Positions Supervised:&nbsp; Hospice Aides</p>
<p>Interrelationships:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Patients, family, IDG and other health care team members</p>
<p>&nbsp;</p>
<p><strong>JOB SUMMARY:</strong></p>
<p>&nbsp;</p>
<p>To provide nursing care to the terminally ill Hospice patient as needed. To provide assistance and understanding to the family in the home care situation and in time of bereavement. To work as a member of the Hospice team in providing Hospice care.</p>
<p>&nbsp;</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>&nbsp;</p>
<p>Educational/Degree:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Graduate from an accredited school of nursing</p>
<p>&nbsp;</p>
<p>Training/Licensure:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Currently licensed as a registered nurse to practice in the state.</p>
<p>&nbsp;</p>
<p>Knowledge/Skills/Ability:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ability to work independently, make accurate, and at times, quick judgments. Ability to supervise others appropriately. Ability to respond appropriately to crisis outside of a hospital setting. Acceptance of and adaptability to different social, racial, cultural and religious modes. Completes Hospice training program.</p>
<p>&nbsp;</p>
<p>Experience:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Minimum 2 years of experience as a registered nurse, preferred.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Active patient contact within past three years, preferred.</p>
<p>&nbsp;</p>
<p><strong>JOB FACTORS:</strong></p>
<p>&nbsp;</p>
<p><strong><u>Physical Requirements:</u></strong></p>
<p>Requires considerable physical effort most of the day including kneeling, squatting, reaching, twisting, climbing, walking, exposure to temperature and humidity changes and maximal assist in lifting and/or transferring of a 100 pound patient. Must possess sight/hearing senses or use appropriate adaptive devices that will enable senses to function at a level required to meet the essential duties of the position. Must provide evidence of annual TB test and other state-required tests or examinations.</p>
<p>&nbsp;</p>
<p><strong><u>Mental Requirements:</u></strong></p>
<p>Must be able to work independently, make judgments based on assessments and data available and act accordingly. Must be flexible, innovative and possess good interpersonal skills. Must be able to cope with mental and emotional stress and demonstrate emotional stability.</p>
<p>&nbsp;</p>
<p><strong><u><br /> </u></strong></p>

<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>Working Conditions:</u></strong></p>
<p>Be able to tolerate exposure to elements including, but not limited to, odors, blood, body fluids and excrements, adverse environmental conditions and hazardous materials.</p>
<p><strong>&nbsp;</strong></p>
<p><strong><u>Transportation:</u></strong></p>
<p>Must have a current valid driver&rsquo;s license, auto liability insurance and reliable transportation.</p>
<p>&nbsp;</p>
<p><strong>Essential Functions:</strong></p>
<ol>
<li>Regularly assesses and reassesses the nursing needs of the Hospice patient.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Provides dietary counseling.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Provides Hospice nursing services, treatments and preventive procedures.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Initiates nursing procedures appropriate for the patient&rsquo;s Hospice care and safety.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Observes signs and symptoms and reports to the physician and IDG members any unexpected changes in the patient&rsquo;s physical or emotional condition.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Teaches, supervises and counsels the Hospice patient and family members about providing care for the patient.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Supervises and trains other nursing service personnel.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Develops and re-evaluates the patient/family care plan in conjunction with IDG to meet needs and maintain continuity of care.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Performs specific nursing procedures as needed (e.g., treatments, management of symptoms) following doctor&rsquo;s orders.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Attends team conferences.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Maintains records as required by Hospice.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Follows the policies and procedures of Hospice. Observes confidentiality and safeguards all patient-related information in compliance with HIPAA regulations.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Always communicates to the supervisor if unable to meet a patient&rsquo;s need or perform a procedure.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Participates in on-call system and is responsible for providing on-call coverage when unavailable for assigned duties.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Maintain skills and knowledge.</li>
</ol>
<p>&nbsp;</p>
<p><strong><em>Job Description &ndash; Registered Nurse&hellip;.continued</em></strong></p>
<p>&nbsp;</p>
<ol>
<li>Works with interdisciplinary group concept of patient care.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Coordinates the implementation of the plan of care for patients residing in SNF, NF, ICF or MR.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Organizes work schedule and utilizes time management to be able to attend all required meetings.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Complies with agency infection control policies and protocols.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Assist with orientation, teaching and training as requested.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Other duties as assigned by Director.</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>I have read the above job description and understand the duties and responsibilities associated with the position. I can perform the essential functions of this position without specific accommodations.</p>
