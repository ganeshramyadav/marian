<p><strong>JOB DESCRIPTION</strong></p>
<p><strong>Social Worker/Social Work Assistant</strong></p>
<p>&nbsp;</p>
<p><strong>REPORTING RELATIONSHIP:</strong></p>
<p>&nbsp;</p>
<p>Supervised by:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Director/Manager of Patient Services/Medical Director</p>
<p>Positions Supervised:&nbsp; N/A</p>
<p>Interrelationships:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Patients, family, IDG and other health care team members</p>
<p>&nbsp;</p>
<p><strong>JOB SUMMARY:</strong></p>
<p>To deliver varied social work services to Hospice patients and their families. To provide initial emotional, spiritual, psychosocial assessments, ongoing counseling, bereavement services and community education, outreach and referral. The Hospice social worker is an integral part of the Hospice IDG.</p>
<p>&nbsp;</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>Educational/Degree and&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; A Hospice Social Worker must at least meet one of the following</p>
<p>Training/Licensure:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; options:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<ol>
<li>Have an MSW degree from a school of social work accredited by the Council on Social Work Education (CSWE), and one year of experience in a health care setting.</li>
<li>Have a baccalaureate degree in social work (BSW) from a school of social work accredited by the CSWE, and one year of experience in a health care setting and be supervised by a MSW from a school of social work accredited by the CSWE and who has one year of experience in a health care setting. If the BSW is employed by the Hospice before December 2, 2008, he//she is exempted from the MSW supervision requirement.</li>
<li>Have a baccalaureate degree in psychology, sociology or other field related to social work, and at least one year of social work experience in a health care setting and be supervised by a MSW from a school of social work accredited by the CSWE and who has one year of experience in a health care setting.</li>
</ol>
<p>&nbsp;</p>
<p>Hospice must also defer to State law regarding social work requirements. If State requirements are more stringent, Hospice must comply with the State requirements. For example, if the State requires a social worker to have a BSW or an MSW, the Hospice may not employ a person with a baccalaureate degree in psychology, sociology or other field related to social work to work as a Hospice Social Worker.</p>
<p>&nbsp;</p>
<p>Hospice must employ or contract with at least one MSW to serve in the supervisor role as an active advisor, consulting with the BSW on assessing the needs of patients and families, developing and updating the social work portion of the plan of care and delivering care to patients and families. Supervision may occur in person, over the telephone, through electronic communication or any combination thereof. Hospice will allow time for the supervision to</p>
<p>&nbsp;</p>

<p>&nbsp;</p>
<p>happen on a regular basis and provide documentation as to the nature and scope of supervision. Hospice also ensures that non-social work trained bachelor&rsquo;s prepared employees filling the role of social worker are supervised by a MSW who graduated from a school of social work accredited by the CSWE and who has at least one year of experience in a health care setting.</p>
<p>&nbsp;</p>
<p>Social workers with a baccalaureate degree from a school of social work accredited by the CSWE and who are employed by Hospice before December 2, 2008, are exempted from the MSW supervision requirement. If Hospice hires a new Social Worker with a baccalaureate degree and one year of experience in a health care setting after December 2, 2008, then the baccalaureate Social Worker must be supervised by an MSW who has one year of experience in a health care setting.</p>
<p>&nbsp;</p>
<p>Knowledge/Skills/Ability:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ability to work independently, make accurate, and at times, quick judgments. Ability to respond appropriately to crisis outside of a hospital setting. Acceptance of and adaptability to different social, racial, cultural and religious modes.</p>
<p>&nbsp;</p>
<p>Experience:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Minimum 2 years of experience as a social worker, preferred.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Active patient contact within past three years, preferred.</p>
<p>&nbsp;</p>
<p><strong>JOB FACTORS:</strong></p>
<p>&nbsp;</p>
<p><strong><u>Physical Requirements:</u></strong></p>
<p>Requires minimal physical effort most of the day including kneeling, squatting, reaching, twisting, climbing, walking, exposure to temperature and humidity changes and minimal assist in lifting and/or transferring of a 20 pound patient. Must possess sight/hearing senses or use appropriate adaptive devices that will enable senses to function at a level required to meet the essential duties of the position. Must provide evidence of annual TB test and other state-required tests or examinations.</p>
<p>&nbsp;</p>
<p><strong><u>Mental Requirements:</u></strong></p>
<p>Must be able to work independently, make judgments based on assessments and data available and act accordingly. Must be flexible, innovative and possess good interpersonal skills. Must be able to cope with mental and emotional stress and demonstrate emotional stability.</p>
<p>&nbsp;</p>
<p><strong><u>Working Conditions:</u></strong></p>
<p>Be able to tolerate exposure to elements including, but not limited to, odors, blood, body fluids and excrements, adverse environmental conditions and hazardous materials.</p>
<p><strong>&nbsp;</strong></p>
<p><strong><u>Transportation:</u></strong></p>
<p>Must have a current valid driver&rsquo;s license, auto liability insurance and reliable transportation.</p>
<p>&nbsp;</p>
<p><strong>Essential Functions:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Performs initial psychosocial, emotional, spiritual and bereavement assessments and assists in the development and implementation of goal-directed IDG care plan.</li>
</ol>
<p>&nbsp;</p>
<p><strong><em>Job Description &ndash; Social Worker/Social Work Assistant&hellip;continued</em></strong></p>
<p>&nbsp;</p>
<ol>
<li>Conducts ongoing reassessments of patient/family needs and counseling as required.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Provides short term crisis intervention and individual or family counseling when indicated.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Provides services, under the direction of a physician (who approves plan of care).</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Participates as a member of the Bereavement Team as assigned.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Observes, assesses and brings to IDG conferences information regarding psychosocial, emotional, spiritual, physical and financial conditions affecting the patient and family.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Assumes the active role of advocate for the patient/family unit.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Develops and maintains contact with appropriate community agencies and services in order to promote interagency cooperation and to facilitate related referrals.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Documents comprehensive psychosocial, emotional and spiritual assessment clearly and concisely in a timely manner.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>All patient/family visits, telephone contacts and referral actions are recorded in the patient record per policy.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Provides ongoing counseling related to issues of death and dying to the patient and family as needed.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Attends staff meetings, IDG and other meetings as assigned and appropriate.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Participates in the orientation program as assigned.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Adheres to all Hospice policies.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Assumes responsibility for own personal and professional development and maintenance of skills in social work.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Exhibits Hospice philosophy in all job related roles.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Other duties as assigned by Director/Manager of Patient Services.</li>
</ol>
<p>&nbsp;</p>
<p>I have read the above job description and understand the duties and responsibilities associated with the position. I can perform the essential functions of this position without specific accommodations.</p>
