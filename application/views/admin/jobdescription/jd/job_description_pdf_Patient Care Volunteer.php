<p><strong>JOB DECSRIPTION</strong></p>
<p><strong>Patient Care Volunteer Coordinator</strong></p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>REPORTING RELATIONSHIP:</strong></p>
<p>&nbsp;</p>
<p>Supervised by:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Director</p>
<p>Positions Supervised:&nbsp; Volunteers</p>
<p>Interrelationships:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Patients, family, IDG and other health care team members</p>
<p>&nbsp;</p>
<p><strong>JOB SUMMARY:</strong></p>
<p>&nbsp;</p>
<p>To maintain and coordinate the volunteer program for Hospice. Responsible for the orientation, training and coordination of all Hospice volunteers, for volunteer program administration and development in all service areas.</p>
<p>&nbsp;</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>&nbsp;</p>
<p>Educational/Degree:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; High school diploma.</p>
<p>&nbsp;</p>
<p>Training/Licensure:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Completes Hospice training program.</p>
<p>&nbsp;</p>
<p>Knowledge/Skills/Ability:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ability to work independently, make accurate, and at times, quick judgments. Ability to respond appropriately to crisis outside of a hospital setting. Acceptance of and adaptability to different social, racial, cultural and religious modes.</p>
<p>&nbsp;</p>
<p>Experience:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Minimum 2 years of experience in a related field, preferred.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Volunteer activity, preferred.</p>
<p>&nbsp;</p>
<p><strong>JOB FACTORS:</strong></p>
<p>&nbsp;</p>
<p><strong><u>Physical Requirements:</u></strong></p>
<p>Requires minimal physical effort most of the day including kneeling, squatting, reaching, twisting, climbing, walking, exposure to temperature and humidity changes and minimal assist in lifting and/or transferring of a 20 pound patient. Must possess sight/hearing senses or use appropriate adaptive devices that will enable senses to function at a level required to meet the essential duties of the position. Must provide evidence of annual TB test and other state-required tests or examinations.</p>
<p>&nbsp;</p>
<p><strong><u>Mental Requirements:</u></strong></p>
<p>Must be able to work independently, make judgments based on assessments and data available and act accordingly. Must be flexible, innovative and possess good interpersonal skills. Must be able to cope with mental and emotional stress and demonstrate emotional stability.</p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u><br /> </u></strong></p>

<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>Working Conditions:</u></strong></p>
<p>Be able to tolerate exposure to elements including, but not limited to, odors, blood, body fluids and excrements, adverse environmental conditions and hazardous materials.</p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>Transportation:</u></strong></p>
<p>Must have a current valid driver&rsquo;s license, auto liability insurance and reliable transportation.</p>
<p>&nbsp;</p>
<p><strong>Essential Functions:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Plan and supervise delivery of all volunteer services.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Assign volunteers to service on the basis of program needs and the volunteers&rsquo; interests and skills.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Assess and monitor a record-keeping system which includes services delivered and actual time involved.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Recruit, interview and select volunteers.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Design and supervise the orientation and training of volunteers.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Monitor and evaluate volunteers&rsquo; performance.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Assure volunteers&rsquo; compliance with Hospice policies and procedures.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Plan and conduct volunteer support meetings.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Prepare services reports as required by the Director.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Facilitate community awareness and support of Hospice volunteer program.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Maintain relationship with other program leaders.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Attend Hospice IDG meetings and act as liaison between volunteers and IDG.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Adhere to Hospice standards and consistently interpret and accurately perform all assigned responsibilities.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Comply with Hospice infection control policies and protocols.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Works with IDG concept of patient care.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Participate in inservice programs and present inservices as assigned.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>Completes Hospice training program.</li>
</ol>
<p>&nbsp;</p>

<p>&nbsp;</p>
<ol>
<li>Performs other duties as assigned by Director.</li>
</ol>
