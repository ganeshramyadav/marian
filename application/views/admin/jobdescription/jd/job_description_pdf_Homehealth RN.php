<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Registered Nurse (RN)</em></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Supervisor/Nursing Supervisor</em></p>
<p><strong>&nbsp;</strong></p>
<h3><strong>Job Description Summary</strong></h3>
<p>&nbsp;</p>
<p>The registered nurse plans, organizes and directs home care services and is experienced in nursing, with emphasis on community health education/experience.&nbsp; The professional nurse builds from the resources of the community to plan and direct services to meet the needs of individuals and families within their homes and communities.</p>
<p><strong>&nbsp;</strong></p>
<h3><strong>Essential Job Functions/Responsibilities</strong></h3>
<p>&nbsp;</p>
<h4>Patient Care</h4>
<p>&nbsp;</p>
<ol>
<li>Completes an initial assessment of patient and family to determine home care needs. Provides a complete physical assessment and history of current and previous illness(es).</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Regularly re-evaluates patient nursing needs.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Initiates the plan of care and makes necessary revisions as patient status and needs change.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Uses health assessment data to determine nursing diagnosis.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Develops a care plan, which establishes goals based on nursing diagnosis and incorporates therapeutic, preventive, and rehabilitative nursing actions. Includes the patient and the family in the planning process.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Initiates appropriate preventive and rehabilitative nursing procedures. Administers medications and treatments as prescribed by the physician.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Counsels the patient and family in meeting nursing and related needs.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Provides health care instructions to the patient as appropriate per assessment and plan of care.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Identifies discharge planning needs as part of the care plan development and implements prior to discharge of the patient.</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
<ol start="10">
<li>Acts as Case Manager when assigned by Clinical Supervisor/Nursing Supervisor and assumes responsibility to coordinate patient care for assigned caseload.</li>
</ol>
<h4><strong>&nbsp;</strong></h4>
<p><strong><em>&nbsp;</em></strong><strong>&nbsp;</strong></p>
<h4><strong>Communication</strong></h4>
<p>&nbsp;</p>
<ol>
<li>Prepares clinical notes and updates the primary physician when necessary and at least every sixty days.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Communicates with the physician regarding the patient&rsquo;s needs and reports any changes in the patient&rsquo;s condition; obtains/receives physician&rsquo;s orders as required.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Communicates with community health related persons to coordinate the care plan.</li>
</ol>
<h4><strong>&nbsp;</strong></h4>
<h4><strong>&nbsp;</strong></h4>
<h4><strong>Additional Duties</strong></h4>
<p>&nbsp;</p>
<ol>
<li>Participates in on-call duties as defined by the on-call policy.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Ensures that arrangements for equipment and other necessary items and services are available.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Instructs, supervises and evaluates home health aide care provided every two (2) weeks.</li>
</ol>
<p><strong>&nbsp;</strong></p>
<p><strong>&nbsp;</strong></p>
<p><strong>&nbsp;</strong></p>
<h3><strong>Position Qualifications</strong></h3>
<p>&nbsp;</p>
<ol>
<li>Registered nurse with current licensure to practice professional nursing in the state.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Graduate of National League for Nursing Accredited School of Nursing.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Maintains a current CPR certification.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Must be a licensed driver with an automobile that is insured in accordance with state/or organization requirements and is in good working order.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Minimum of two years of experience, at least one of which is in the area of public health or home care nursing is preferred.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Self-directed and able to work with minimal supervision.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Demonstrates excellent observation, problem solving, verbal and written communication skills; nursing skills per competency checklist.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Shows ability to organize and prioritize workload independently.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Management experience not required. Responsible for supervising home health aides.</li>
</ol>
<p>&nbsp;</p>
<ol start="10">
<li>Prolonged or considerable walking or standing. Able to lift, position, or transfer patients.&nbsp; Able to lift supplies and equipment.&nbsp; Considerable reaching, stooping, bending, kneeling, or crouching.&nbsp; Visual acuity and hearing to perform required nursing skills.</li>
</ol>
