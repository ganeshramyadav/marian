<p style="text-align: center;"><strong>JOB DESCRIPTION</strong></p>
<p style="text-align: center;"><strong>PHYSICAL THERAPIST</strong></p>
<p><strong>&nbsp;</strong></p>
<ol>
<li>The physical therapist must be qualified and licensed by the State.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>The physical therapist shall provide care treatment in accordance with the plan of care, which is ordered by the physician.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>The functions of the physical therapist shall include the following:</li>
<li>Assisting the physician in evaluating patient&rsquo;s level of function.</li>
<li>Assisting the physician in developing and updating the plan treatment.</li>
<li>Preparing therapy and progress notes</li>
<li>Observing, recording and reporting information the patient&rsquo;s condition to the attending physician and in the patient&rsquo;s health records.</li>
<li>Advising, consulting and, when appropriate, instructing family and other agency personnel, in patient&rsquo;s therapy program.</li>
<li>Teaching and supervising other health personnel when appropriate.</li>
<li>Evaluating the home environment and making appropriate recommendation.</li>
<li>Participating in in- service education programs.</li>
</ol>
<p>&nbsp;</p>
<ol>
<li>The physical therapist shall perform the following duties in regard to Patient Care Activities:</li>
<li>Initial evaluation to determine level of function.</li>
<li>Problem identification.</li>
<li>Goal setting.</li>
<li>Care planning in consultation with the physician to achieve goals</li>
<li>Interim evaluation.</li>
<li>Revision of plan of care as directed.</li>
<li>Consultation with team members.</li>
<li>Patient/family education.</li>
<li>Provision of equipment required increasing client&rsquo;s function and independence.</li>
<li>Discharge planning and evaluation.</li>
<li>Timely recording of all evaluation data, treatments, and patient&rsquo;s response to therapy interventions.</li>
<li>Recording of conference outcome.</li>
<li>Recording of quality assurance data.</li>
</ol>
