<p><strong>Job Title/Position</strong>:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Certified Home Health Aide</em></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Supervisor</em></p>
<h3>Job Description Summary</h3>
<p>The home health aide is a paraprofessional member of the home care team who works under the supervision of a registered nurse or therapist and performs various personal care services as necessary to meet the patient&rsquo;s needs.&nbsp; The home health aide is responsible for observing patients, reporting these observations and documenting observations and care performed.</p>
<p>The home health aide will be assigned in a manner that promotes quality, continuity and safety of a patient&rsquo;s care.</p>
<h3>Essential Job Functions/Responsibilities</h3>
<p>Responsibilities of the home health aide include, but are not limited to, the following:</p>
<ol>
	<li>Providing personal care including:</li>
	<li>Baths</li>
	<li>Back rubs</li>
	<li>Oral hygiene</li>
	<li>Shampoos</li>
	<li>Changing bed linen</li>
	<li>Assisting patients with dressing and undressing</li>
	<li>Skin care to prevent breakdown</li>
	<li>Assisting the patient with toileting activities</li>
	<li>Keeping patient&rsquo;s living area clean and orderly, as appropriate</li>
	<li>Planning and preparing nutritious meals.</li>
	<li>Assisting in feeding the patient, if necessary.</li>
	<li>Taking and recording oral, rectal and axillary temperatures, pulse, respiration and blood pressure when ordered (with appropriate completed/demonstrated skills competency).</li>
	<li>Assisting in ambulation and exercise according to the plan of care.</li>
	<li>Performing range of motion and other simple procedures as an extensional therapy service as ordered (with appropriate completed/demonstrated skills competency).</li>
	<li>Assisting patient in the self-administration of medication.</li>
	<li>Doing patient&rsquo;s laundry, as appropriate.</li>
	<li>Meeting safety needs of patients and using equipment safely and properly (foot stools, <br /> side rails, etc.).</li>
	<li>Reporting on patient&rsquo;s condition and significant changes to the assigned nurse.</li>
	<li>Adhering to the Organization's documentation and care procedures and standards of personal and professional conduct.</li>
</ol>
<p>The above statements are only meant to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job related tasks other than those stated in this description.</p>
<h3>Position Qualifications</h3>
<ol>
	<li>Meets the training requirements in accordance with State and Federal laws.</li>
	<li>At least 18 years of age.</li>
	<li>Ability to read and follow written instructions and document care given.</li>
	<li>Self-directing with the ability to work with little direct supervision.</li>
	<li>Empathy for the needs of the ill, injured, frail and the impaired.</li>
	<li>Possess and maintains current CPR certification.</li>
	<li>Demonstrates tact, patience and good personal hygiene.</li>
	<li>Licensed driver with automobile that is insured in accordance with Organization requirements and is in good working order.</li>
</ol>
<p>{<strong>Note: </strong>Effective August 14, 1990, a person who has successfully completed a state established or other training program that meets the requirements of CFR 484.36(a) and a competency evalu&shy;ation program, or state licensure program that meets the requirements of CFR 484.36(b), or a competency evaluation program or state licensure program that meets the requirements of S 484.36(b).}</p>
<h3>Job Limitations</h3>
<p>The home health aide will not function in any manner viewed as the practice of nursing according to the State's Nurse Practice Act.&nbsp; Specifically, the home health aide will not administer medications, take physician's orders or perform procedures requiring the training, knowledge and skill of a nurse, such as sterile techniques.</p>
