<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Referral/Intake Supervisor</em></p>
<p><strong>&nbsp;</strong></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Executive Director/Administrator</em></p>
<p><em>&nbsp;</em></p>
<h3><strong>Job Description Summary</strong></h3>
<p>&nbsp;</p>
<p>Responsible for managing all aspects of the patient intake process including managing the members of the intake team, establishing and maintaining positive relationships with customers and referral sources, responding to customer requests and concerns, and managing the insurance verification and authorization processes.</p>
<p>&nbsp;</p>
<h3><strong>Essential Job Functions/Responsibilities</strong></h3>
<ol>
<li>Directs all daily patient referral and intake operations including providing direct oversight of the establishment and implementation of intake policies.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Ensures compliance with all state, federal, and Joint Commission referral/intake regulatory requirements.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Directs the implementation of improved work methods and procedures to ensure patients are admitted in accordance with policy.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Establishes and maintains positive working relationships with current and potential <br /> referral sources.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Ensures seamless transition of patients to home care by providing direct oversight of patient education and preparation for home care, plan of care initiation, and coordination of care with multiple service providers.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Ensures maximum third party reimbursement through direct oversight of insurance verification and authorization processes.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Negotiates service pricing with insurance Case Managers and other payers within established financial and credit parameters.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Assists the Executive Director/Administrator in the preparation of an annual budget for the intake department and monitors allocation of resources according to budgetary limitations.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Recruits, selects, orients, and directly manages members of the referral/intake team.</li>
</ol>
<p>&nbsp;</p>
<ol start="10">
<li>Builds and monitors community and customer perceptions of Lifelink Health Providers, Inc. as a high quality provider of services.</li>
</ol>
<p>&nbsp;</p>
<ol start="11">
<li>Gathers, collates, and reports referral statistics including key customer referral trends.</li>
</ol>
<p>&nbsp;&nbsp;</p>
<ol start="12">
<li>Provides leadership in strategic planning including identifying opportunities for additional or improved services to meet customer needs.</li>
</ol>
<p>&nbsp;</p>
<ol start="13">
<li>Maintains comprehensive working knowledge of Lifelink Health Providers, Inc. contractual relationships and ensures that patients are admitted according to contract provisions.</li>
</ol>
<p>&nbsp;</p>
<ol start="14">
<li>Maintains comprehensive working knowledge of community resources and assists referral sources in accessing community resources should services not be provided by Lifelink Health Providers, Inc.</li>
</ol>
<p>&nbsp;</p>
<p>The above statements are intended to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job-related tasks other than those stated in this description.</p>
<p>&nbsp;</p>
<h3><strong>Position Qualifications</strong></h3>
<p>&nbsp;</p>
<ol>
<li>Registered nurse with current licensure to practice professional nursing in the state.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Bachelor&rsquo;s degree in Nursing from an accredited program by the National League for Nursing. Master&rsquo;s degree in health care preferred.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>At least three to five years of experience in health care management, preferably in home <br /> care operations.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Demonstrated ability to supervise and direct professional and administrative personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Ability to market aggressively and deal tactfully with customers and the community.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Knowledge of corporate business management, governmental regulations, Joint Commission standards, and private payer practices.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Demonstrates good communication, negotiation, and public relations skills.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Demonstrates autonomy, organization, assertiveness, flexibility and cooperation in performing job responsibilities.</li>
</ol>
