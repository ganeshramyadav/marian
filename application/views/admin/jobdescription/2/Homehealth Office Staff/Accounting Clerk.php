<p><strong>Job Title/Position:</strong><em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </em><em>Accounting Clerk</em></p>

<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Finance Director</em></p>
<p>&nbsp;</p>
<p><strong>Job Description Summary</strong></p>
<p>Responsible for managing the transaction accounting and asset accounting functions of the organization including general ledger management, accounts payable management, payroll management, and financial reporting.</p>
<p><strong>Essential Job Functions/Responsibilities</strong></p>
<ol>
	<li>Manages all daily accounting operations and accounting information systems including general ledger functions, accounts payable functions, payroll functions, asset accounting and financial reporting.</li>
	<li>Ensures financial record systems are maintained in accordance with generally accepted accounting principles and in compliance with state, federal and Joint Commission regulations.</li>
	<li>Assists the Director of Finance in the preparation of the annual budget and prepares budget variation reports.</li>
	<li>Protects the assets of the organization through effective internal controls, internal auditing, and direct supervision of accounting operations.</li>
	<li>Manages and controls cash disbursements through an effective accounts payable system.</li>
	<li>Prepares and files periodic financial statements for the Director of Finance.</li>
	<li>Gathers, collates, and reports key statistics through the effective utilization of  information systems.</li>
	<li>Provides suggestions in strategic financial planning including identifying opportunities for improved fiscal viability.</li>
	<li>Assists the Director of Finance in the preparation of the annual Medicare cost report.</li>
	<li>Prepares and reports all applicable federal, state and local taxes.</li>
	<li>Manages payroll practices and assures compliance with state and federal regulations, including wages, salaries and benefit regulations.</li>
	<li>Prepares all requested documentation for annual internal and external audits.</li>
	<li>Maintains patient and organization confidentiality at all times.</li>
</ol>

<p>The above statements are intended to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job-related tasks other than those stated in this description.</p>

<h3><strong>Position Qualifications</strong></h3>

<ol>
	<li>Bachelor's degree in Accounting, CPA preferred.</li>
	<li>At least three years of experience in health care accounting management preferably in home care operations. Information systems knowledge required.</li>
	<li>Demonstrated ability to supervise and direct professional and administrative personnel.</li>
	<li>Knowledge of corporate business management, governmental regulations and Joint Commission standards.</li>
	<li>Demonstrates good communications and public relations skills.</li>
</ol>

