<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Job
Description Summary</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">The
medical social worker is responsible for the implementation of
standards of care for medical social work services.</font></p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Essential
Job Functions/Responsibilities</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Assesses
	the psychosocial status of patients related to the patient’s
	illness and environment and communicates findings to the registered
	nurse.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Carries
	out social evaluations and plans intervention based on evaluation
	findings.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Maintains
	clinical records on all patients referred to social work.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="4">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Provides
	information and referral services for Organization patients and
	families/caregivers regarding practical and environmental needs.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="5">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Provides
	information to patients or families/caregivers and community
	agencies.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="6">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Serves
	as liaison between patients or families/caregivers and community
	agencies.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="7">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Maintains
	collaborative relationships with Organization personnel to support
	patient care.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="8">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Maintains
	and develops contracts with public and private agencies as resources
	for patient and organization personnel.</font></font></p>
</li></ol>
<p style="margin-left: 0.5in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="9">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Participates
	in in-service education programs.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="10">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Participates
	in the development of the total plan of care and case conferences as
	required.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="11">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Assists
	physician and other team members in understanding significant social
	and emotional factors related to health problems.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="12">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Participates
	in discharge planning.</font></font></p>
</li></ol>
<p style="margin-left: 0.3in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-left: 0.3in; margin-bottom: 0in; line-height: 100%"><br>
