<p><strong>Job Title/Position:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong><em>Executive Director/Administrator</em></p>
<p><strong>&nbsp;</strong></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Governing Body</em></p>
<p><strong>&nbsp;</strong></p>
<p><strong>Job Description Summary</strong></p>
<p>Responsible for planning, coordinating, managing and directing all activities and programs of the Lifelink Home Health Providers, Inc.</p>
<p>&nbsp;</p>
<p><strong>Essential Job Functions/Responsibilities:</strong></p>
<ol>
<li>Planning and directing operations to ensure the provision of adequate and appropriate care and services.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Fiscal planning, budgeting and management.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Ensuring organizational compliance with legal, regulatory and accreditation standards. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Recruiting, employing and retaining qualified personnel to maintain appropriate staffing levels.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Establishing and maintaining effective open channels of communication. Fosters a no-blame culture that encourages reporting of safety and quality of services issues.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Ensuring program personnel have current clinical information and current practices.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Ensuring ongoing oversight of the agency&rsquo;s quality management system.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Evaluating services and programs. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Ensuring personnel development including orientation, inservice education and continuing education.</li>
</ol>
<p>&nbsp;</p>
<ol start="10">
<li>Coordinating with other program areas and management as appropriate.</li>
</ol>
<p>&nbsp;</p>
<ol start="11">
<li>Maintaining current on local trends and issues.</li>
</ol>
<p>&nbsp;</p>
<ol start="12">
<li>Ensuring that appropriate service policies are developed and implemented.</li>
</ol>
<p>&nbsp;</p>
<ol start="13">
<li>Directing personnel in performance of their duties including admission, discharge and provision of service to patients.</li>
</ol>
<p>&nbsp;</p>
<ol start="14">
<li>Assuring appropriate personnel supervision during all operating hours.</li>
</ol>
<p>&nbsp;</p>
<ol start="15">
<li>Ensuring the accuracy of public information materials and activities</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
<ol start="16">
<li>Appointing a similarly qualified alternate to be available at all times during operating hours in the absence of the administrator.</li>
</ol>
<p>&nbsp;</p>
<p>The above statements are intended to be a representative summary of the major duties and responsibilities performed by incumbents of this job. The incumbents may be requested to perform job-related tasks other than those stated in this description.</p>
