<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp; <em>Finance Director</em></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Executive Director/Administrator</em></p>
<p>&nbsp;</p>
<p><strong>Job Description Summary</strong></p>
<p>Responsible for planning, coordinating, managing and directing the financial performance of the Lifelink Health Providers,&nbsp; Inc. including budget preparations, financial and statistical reporting, cost reporting, accounts payable, billing and accounts receivable management.</p>
<h3>&nbsp;</h3>
<h3><strong>Essential Job Functions/Responsibilities:</strong></h3>
<p>&nbsp;</p>
<ol>
<li>Directs all daily financial business operations and implements financial policies, accounting systems and cost controls.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Directs the implementation of improved work methods and procedures to ensure financial record systems are maintained in accordance with generally accepted accounting principles and in compliance with state, federal and Joint Commission regulations.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Directs the management of all accounting information systems including payroll, <br /> accounts payable, accounts receivable, general ledger, financial reporting, and statistical reporting systems.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Ensures maximum third party reimbursement through efficient billing and collections operations, effective accounts receivable management and a thorough understanding of cost reimbursement principles.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Assists the Executive Director/Administrator in the preparation of the annual budget, monitors allocation of funds within budgetary limitations, prepares budget variation reports for the review and approval of the executive director and Governing Body.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Protects the assets of the organization through effective internal controls, internal auditing, insurance coverage, and direct supervision of financial operations.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Ensures proper utilization of organization resources through control of cash disbursements through an effective accounts payable system.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Prepares and files periodic financial statements for internal users, authorized third parties and regulatory agencies on an ongoing basis.</li>
</ol>
<p>&nbsp;&nbsp;</p>
<ol start="9">
<li>Evaluates financial provisions of proposed contracts and agreements for provision of services to optimize financial viability of the agreements.</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
<ol start="10">
<li>Gathers, collates, and reports key operating statistics through the effective utilization of information systems.</li>
</ol>
<p>&nbsp;</p>
<ol start="11">
<li>Provides leadership in strategic financial planning including identifying opportunities for improved fiscal viability.</li>
</ol>
<p>&nbsp;</p>
<ol start="12">
<li>Prepares the annual Medicare cost report, and serves as the organization liaison to the Medicare fiscal intermediary.</li>
</ol>
<p>&nbsp;</p>
<ol start="13">
<li>Prepares and reports all applicable federal, state and local taxes.</li>
</ol>
<p>&nbsp;</p>
<ol start="14">
<li>Prepares the financial component of the annual report on the organization&rsquo;s activity.</li>
</ol>
<p>&nbsp;</p>
<ol start="15">
<li>Directs payroll practices and collaborates with other members of the management team in establishing personnel policies that assures compliance with state and federal regulations, including wages, salaries and benefit regulations.</li>
</ol>
<p>&nbsp;</p>
<ol start="16">
<li>Acts as the financial liaison to the Governing Body and attends meetings as requested by the executive director.</li>
</ol>
<p>&nbsp;</p>
<ol start="17">
<li>Recruits, selects, and orients new personnel and arranges for inservice education, as appropriate.</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;&nbsp;</p>
<p>The above statements are intended to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job-related tasks other than those stated in this description.</p>
<h3>&nbsp;</h3>
<h3><strong>&nbsp;</strong><strong>&nbsp;</strong></h3>
<h3><strong>Position Qualifications:</strong></h3>
<ol>
<li>Bachelor's degree in Accounting, Finance, Business Administration or related field, CPA and Master&rsquo;s Degree preferred.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>At least three to five years of experience in health care financial management preferably in home care operations. Information systems knowledge required.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Demonstrated ability to supervise and direct professional and administrative personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Knowledge of corporate business management, governmental regulations and <br /> Joint Commission standards.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Demonstrates good communications and public relations skills.</li>
</ol>
