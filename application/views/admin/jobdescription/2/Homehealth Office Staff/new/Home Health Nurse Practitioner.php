<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Home Health Nurse Practitioner</em></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Director</em></p>
<p>&nbsp;</p>
<h3><strong>Job Description Summary</strong></h3>
<p>&nbsp;</p>
<p>The Nurse Practitioner is a registered professional nurse with advanced graduate education and clinical training who provides comprehensive health care services in compliance with the state Nurse Practice Act and under the supervision of a collaborating physician. He/she provides comprehensive care through the evaluation and assessment of the health history, health promotion, diagnosing medical conditions, management of health problems by directing and developing the plan of care, prescribing medications or treatments, coordinating and collaborating with patients, families and other healthcare practitioners. Under the supervision of a physician, the nurse practitioner visits patients and completes physical assessments as assigned in consultation with the primary care team. The nurse practitioner builds from the resources of the community to plan and direct services to meet the need of individuals and families within their homes and communities.</p>
<p>&nbsp;</p>
<h3><strong>Essential Job Functions/Responsibilities</strong></h3>
<p><strong>&nbsp;</strong></p>
<h4><strong>Patient Care</strong></h4>
<p><strong><em>&nbsp;</em></strong></p>
<ol>
<li>Provides medical care in compliance with the state Nurse Practice Act under the supervision of a collaborating physician.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Completes history and physical examinations by evaluating the patient's medical condition and health history, ordering and interpreting results from laboratory and diagnostic tests or procedures, diagnosing health conditions and documents the findings in the patient's medical record.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Manages health conditions by developing an individualized plan of care, prescribing medications and/or treatments, obtaining consultations or making referrals and coordinating health care services.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Reviews clinical information, coordinates and oversees patient care in consultation with the primary care team and other healthcare practitioners.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Assures that patient receives appropriate measures to control symptoms through collaboration with the primary care team members.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Reviews and develops protocols for treatment and proposes options for interventions based on the literature in collaboration with the collaborating physician.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Consults the collaborating physician when the patient's plan of care is outside standardized practice and protocols.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Promotes health by ordering screenings and prescribing preventive therapie</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong><em>Communication</em></strong></p>
<ol>
<li>Communicates and collaborates with other health care practitioners to create, review and revise the patient's plan of care.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Consults with the patient's attending physician, primary care team and other healthcare practitioners regarding the patient's current condition, ongoing care needs and medical management.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Communicates with other community health practitioners to coordinate the plan of care.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Attends and participates in care management meetings as needed.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Completes, maintains and submits accurate and relevant clinical notes, physical examination notes, assessment visit notes, medical orders, collaboration notes and other documentation in the medical record.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Educates/counsels patients, families, and/or caregivers as to preventative care, medical problems, psychological problems, and spiritual problems in conjunction with the primary care team to meet the total needs of patients.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Provides and maintains a safe environment for the patient.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Assists the patient and family/caregiver and other team members in providing continuity of care.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Works in cooperation with the family/caregiver and other health care practitioners to identify and meet the care needs of the patient and family/caregiver.</li>
</ol>
<p>&nbsp;</p>
<ol start="10">
<li>Serves as a nursing resource for consultation and educations in the community.</li>
</ol>
<p>&nbsp;</p>
<ol start="11">
<li>Establishes, builds and nurtures relationships with staff and community referral sources to facilitate program growth.</li>
</ol>
<p>&nbsp;</p>
<h4><strong>Additional Duties</strong></h4>
<p>&nbsp;</p>
<ol>
<li>Maintains knowledge of and compliance with current Medicare/Medicaid, state/federal rules and regulations for home care.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Ensures compliance with the Medicare conditions of participation and other state regulations govern the provision of healthcare.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Complies with all Health Insurance Portability and Accountability Act (HIPAA) requirements in accordance with federal, state and organizational policies.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Participates in organizational monitoring of the quality of medical services and quality improvement initiatives.</li>
</ol>
<p>&nbsp;&nbsp;</p>
<ol start="5">
<li>Assumes responsibility for personal growth. Develops, maintains and upgrades professional knowledge and practice skills through attendance at seminars, conferences and participation in continuing education and in-service classes.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Fulfills the obligation of requested and/or accepted assignments.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Demonstrate knowledge in communication and counseling patient/family in dealing with health care issues.</li>
</ol>
<p><strong>&nbsp;</strong></p>
<h3><strong>Position Qualifications</strong></h3>
<p>&nbsp;</p>
<ol>
<li>Graduation from an accredited School of Nursing.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Completion of an accredited Nurse Practitioner Program which conforms with the Board&rsquo;s educational standards set forth in the California Code of Regulations Section 1484 or certification by a national organization/association whose standards are equivalent to those set forth in the&nbsp; California Code of Regulations Section 1484 or a registered nurse who has not completed a nurse practitioner program of study which <strong>meets </strong>the Board of Registered Nursing&rsquo;s educational standards as specified in the California Code of Regulations Section 1484.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Current nursing licensure in State and CPR certification.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Master's degree with a minimum of (1) year Nurse Practitioner experience.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Accreditation from an approved certifying body for advanced practice nursing as required by State.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Certification in a specialist area preferred; (e.g. Adult, Acute Care, Oncology, Pediatrics or Geriatrics)</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Excellent observation, verbal and written communication skill, problem solving skills, mathematical skills; nursing skills per competency checklist.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Prolonged or considerable walking or standing. Able to lift, position and / or transfer patients. Able to lift supplies and equipment. Considerable reaching, stooping, bending, kneeling and/or crouching. Visual acuity and hearing to perform required nursing skills.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Must be a licensed driver with an automobile that is insured in accordance with state/or organization requirements and is in good working order.</li>
</ol>
