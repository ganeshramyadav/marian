<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Marketing/Community Relations Director</em></p>
<p>&nbsp;</p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Executive Director/Administrator</em></p>
<p><strong>&nbsp;</strong></p>
<p><strong>Job Description Summary</strong></p>
<p>Responsible for managing all aspects of organization marketing/sales including managing the members of the marketing/sales team, establishing and maintaining positive relationships with customers and referral sources, responding to customer requests and concerns, and negotiating service contracts with managed care organizations, government agencies, and other payers.</p>
<p>&nbsp;</p>
<p><strong>Essential Job Functions/Responsibilities</strong></p>
<ol>
<li>Directs all daily marketing operations including providing direct oversight of the establishment and implementation of marketing/sales initiatives.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Ensures maximum third party reimbursement through effective negotiation of service contracts with managed care organizations, government agencies, and other payers.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Assists the Executive Director/Administrator in establishing organization volume projections in the annual budget and in establishing allocations for the marketing department. Monitors allocation of resources according to budgetary limitations.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Continuously conducts market assessments and develops a comprehensive marketing plan designed to meet budgetary volume projections.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Employs marketing and promotional initiatives to achieve budgetary volume projections.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Establishes and maintains positive working relationships with current and potential referral and payer sources.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Negotiates service pricing with insurance Case Managers and other payers within established financial and credit parameters.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Recruits, selects, orients, and directly manages members of the marketing/sales team.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Builds and monitors community, customer, payer and patient perceptions of Lifelink Health Providers, Inc. as a high quality provider of services.</li>
</ol>
<p>&nbsp;</p>
<ol start="10">
<li>Provides leadership in strategic planning including identifying opportunities for additional or improved services to address customer needs.</li>
</ol>
<p>&nbsp;</p>
<ol start="11">
<li>Maintains comprehensive working knowledge of Lifelink Health Providers, markets including government agencies, major payer groups, key referral sources, and competitor&rsquo;s market positioning.</li>
</ol>
<p>&nbsp;&nbsp;</p>
<ol start="12">
<li>Maintains comprehensive working knowledge in the field of marketing and shares information with appropriate organization personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="13">
<li>Maintains comprehensive working knowledge of community resources and assists customers in accessing community resources should services not be provided by LifelinkHealth Providers, Inc.</li>
</ol>
<p>&nbsp;</p>
<ol start="14">
<li>Monitors and reports cost-effectiveness of marketing efforts.</li>
</ol>
<p>&nbsp;</p>
<p>The above statements are intended to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job-related tasks other than those stated in this description.</p>
<p><strong>Position Qualifications</strong></p>
<ol>
<li>Bachelor's degree in Marketing, Business Administration, or related field; Master&rsquo;s Degree preferred.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>At least three years of experience in health care marketing management, preferably in home care operations. At least one year experience in sales is preferred.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Demonstrated ability to supervise and direct professional and administrative personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Ability to market aggressively and deal tactfully with customers and the community.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Knowledge of corporate business management.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Demonstrates excellent communications, negotiation, and public relations skills.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Demonstrates autonomy, organization, assertiveness, flexibility and cooperation in performing job responsibilities.</li>
</ol>
