<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Performance Improvement Coordinator</em></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Director</em></p>
<p><em>&nbsp;</em></p>
<h3><strong>Job Description Summary</strong></h3>
<p>&nbsp;</p>
<p>The Performance Improvement Coordinator is a professional, registered nurse responsible for all of the quality activities of the organization.&nbsp; The Performance Improvement Coordinator has accountabilities for the monitoring, implementation and evaluation of process and programs to meet regulatory, accreditation and standards of practice.</p>
<p>&nbsp;</p>
<h3><strong>Essential Job Functions/Responsibilities</strong></h3>
<p>&nbsp;</p>
<ol>
<li>Responsible for the implementation and monitoring of the organization&rsquo;s quality improvement program.</li>
</ol>
<p>&nbsp;</p>
<ol style="list-style-type: upper-alpha;">
<li>Ensures that there are processes in place to monitor and measure all activities related to performance improvement.</li>
<li>Provides direction and coordination of quality improvement activities utilizing continuous quality improvement principles and methodologies.</li>
<li>Monitors the organization&rsquo;s Outcome Based Quality Initiative (OBQI) outcomes. Provides feedback and best practice education to staff to improve outcomes.</li>
<li>Coordinates activities related to clinical record review for data collection.</li>
<li>Compiles, trends and reports quality data in the following areas: patient care, safety, risk management, infection control, outcomes, and customer satisfaction. Data is analyzed and compared internally and externally with other sources when available.</li>
<li>Works collaboratively with nursing supervisors to ensure documentation is complete and consistent with care and reflects legal requirements.</li>
<li>Member of the Patient Grievance Committee collaborates with management personnel in follow-up of concerns expressed by patients, families, and physicians.</li>
<li>Establishes a process model for improvement and provides education and training to organization personnel on the performance improvement plan, as well as team development.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Responsible for ensuring processes to monitor and evaluate safety, risk management and infection control programs.</li>
</ol>
<p>&nbsp;</p>
<ol style="list-style-type: upper-alpha;">
<li>Coordinates, educates and implements monitoring activities for safety, risk management, and infection control.</li>
<li>Coordinates mandatory in-service programs on OSHA regulations for safety and infection control.Acts as resource to personnel in identifying safety and risk management issues for patients and personnel.</li>
<li>Reviews all incident reports for completeness, risk, standard of care determination and appropriate follow-up. Maintains a log for trending and makes recommendations related to trends identified.</li>
<li>Maintains current knowledge related to home care safety and infection control standards.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Collaborates with nursing supervisors to provide a process to promote and ensure employee orientation, competency and inservice education programs.</li>
</ol>
<p>&nbsp;</p>
<ol style="list-style-type: upper-alpha;">
<li>Assists in the identification of skills for annual competence and provides inservice training support.</li>
<li>Maintains aggregate records of employee orientation and annual competency requirements as part of the PI plan.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Monitor organization compliance with regulatory and accreditation standards.</li>
</ol>
<p>&nbsp;</p>
<ol style="list-style-type: upper-alpha;">
<li>Maintains current knowledge related to Medicare Conditions of Participation and the Joint Commission&rsquo;s standards to ensure that the organization is compliant with state, federal and accreditation guidelines.</li>
<li>Responsible for ensuring policies and procedures reflect all related local, state, federal and Joint Commission standards and makes recommendation for policy changes to the governing board.</li>
<li>Acts as a resource to Clinical Supervisors/Nursing Supervisors regarding standards, policies, regulations and provides inservice training to leadership and personnel as needed in these areas.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Acts as a clinical resource to personnel.</li>
</ol>
<p>&nbsp;</p>
<ol style="list-style-type: upper-alpha;">
<li>Maintains updated nursing knowledge and skills.</li>
<li>Assists with patient visits as needed.</li>
<li>Assists with administrative call on a rotational basis and clinical call as needed.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Provides assistance to community education team in meeting community education needs.</li>
</ol>
<p>&nbsp;</p>
<ol style="list-style-type: upper-alpha;">
<li>Makes public speaking engagements as scheduled by the community education team.</li>
<li>Maintain positive relationships with referral sources.</li>
<li>Maintain positive image in the community.</li>
<li>Acts as a representative of the organization at community meetings, civic and professional organizations as needed.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Promotes self-growth and collegial relationships with others in the home care industry.</li>
</ol>
<p>&nbsp;</p>
<ol style="list-style-type: upper-alpha;">
<li>Maintains professional and technical knowledge by attending educational workshops, reviewing professional publications, establishing personal and professional networks and participates in professional organizations or societies.</li>
</ol>
<p>&nbsp;</p>
<p>The above statements are only meant to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job related tasks other than those stated in this description.</p>
<h3><strong>&nbsp;</strong></h3>
<h3><strong>Position Qualifications</strong></h3>
<p>&nbsp;</p>
<ol>
<li>Registered nurse with current licensure to practice professional nursing in the State.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Bachelor&rsquo;s degree in Nursing from an accredited program by the National League for Nursing.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Three to five year's clinical nursing experience. (An emphasis in home care nursing is preferred).</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Excellent understanding of performance improvement, quality assurance, and <br /> utilization management.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Is self-directed and able to work with minimal supervision.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Has excellent coordination and communication skills.</li>
</ol>
