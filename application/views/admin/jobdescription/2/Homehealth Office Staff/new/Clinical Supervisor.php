<p><strong>Job Title/Position:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong><em>Clinical Supervisor/Nursing Supervisor</em></p>
<p><em>&nbsp;</em></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Director</em></p>
<p>&nbsp;</p>
<h3><strong>Job Description Summary</strong></h3>
<p>&nbsp;</p>
<p>The Clinical Supervisor/Nursing Supervisor is responsible for ensuring that patient care is coordinated and managed appropriately.&nbsp; The Clinical Supervisor/Nursing Supervisor is responsible for ensuring that care and services are delivered appropriately as well as the supervision of clinical personnel.</p>
<p><strong>&nbsp;</strong></p>
<h3><strong>Essential Job Functions/Responsibilities</strong></h3>
<p>&nbsp;</p>
<ol>
<li>Receives case referrals. Reviews available patient information related to case, including disciplines required, to determine home care needs.&nbsp; Assigns appropriate clinicians to case.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Reviews and evaluates each case by reviewing the services provided by clinicians, conferences, record review, discusses and verifies impressions, instructs and guides clinicians to promote more effective performance and delivery of quality home care services, and is available at all times during operating hours to assist clinicians as appropriate.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Reviews patient&rsquo;s clinical diagnosis, medications, procedures and clinical course.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Assists clinicians in establishing immediate and long-term therapeutic goals, in setting priorities, and in developing plan of care.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Attends case conference meetings with organization personnel to facilitate coordination of care.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Conducts quarterly record reviews and communicates findings and recommendations to Clinical Director and appropriate organization personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Assists in the screening and interviewing process of new organization personnel and <br /> makes recommendations for employment of individuals. Assists in the orientation of <br /> new organization personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Assists Clinical Director in the planning, implementation and evaluation of inservice and continuing education programs.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Assists in the formulation, revision, implementation, and evaluation of policies and procedures, as well as strategic goals and objectives.</li>
</ol>
<p><strong><em>&nbsp;</em></strong></p>
<ol start="10">
<li>Complies with accepted professional standards and principles.</li>
</ol>
<p>&nbsp;</p>
<ol start="11">
<li>Participates in public relation and community activities that promote the Organization's role as an effective member of the health care delivery system.</li>
</ol>
<p>&nbsp;&nbsp;</p>
<ol start="12">
<li>Promotes customer service orientation to all organization personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="13">
<li>Performs other duties and activities as delegated by the Clinical Director.</li>
</ol>
<p>&nbsp;</p>
<p>The above statements are only meant to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job related tasks other than those stated in this description.</p>
<p>&nbsp;</p>
<h3><strong>Position Qualifications</strong></h3>
<p>&nbsp;</p>
<ol>
<li>Registered nurse with current licensure to practice professional nursing in the State.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Graduate of National League for Nursing Accredited School of Nursing. BSN degree is preferred.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Two years management or supervisory experience within the past five years in a home health agency, primary care clinic, or health facility, unless the individual has been previously approved for such employment by a program flexibility issued for the individual&rsquo;s current position at the home health agency prior to April 1, 1995.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Knowledge and expertise in clinical decision-making and Quality Management review and evaluation.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Complies with accepted professional standards and practice.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Complies and maintains current CPR certification.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Demonstrates excellent observation, verbal and written communication skills.</li>
</ol>
