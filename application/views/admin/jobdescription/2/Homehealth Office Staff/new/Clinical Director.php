<p><strong>Job Title/Position:</strong><em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </em><em>Clinical Director/Director of Patient Care Services</em></p>
<p>&nbsp;</p>
<p>Reports To:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Executive Director/Administrator</p>
<p>&nbsp;</p>
<p><strong>Job Description Summary</strong></p>
<p>The Clinical Director is responsible for the overall direction of home health clinical services. <br /> The Clinical Director establishes, implements and evaluates goals and objectives for home health services that meet and promote the standards of quality and contribute to the total organization and philosophy.</p>
<p><strong>&nbsp;</strong></p>
<p><strong>Essential Job Functions/Responsibilities</strong></p>
<ol>
<li>Coordinates and oversees all direct and indirect patient services provided by clinical <br /> organization personnel.</li>
</ol>
<ol start="2">
<li>Provides guidance and counseling to coordinators and Clinical Supervisors/Nursing Supervisors to assist them in continually improving all aspects of home health care services, provided through organization personnel.</li>
</ol>
<ol start="3">
<li>Assists Clinical Supervisors/Nursing Supervisors in managing clinical teams and planning.</li>
</ol>
<ol start="4">
<li>Provides help in assessment, planning, implementation and evaluation of patient and family/caregiver care to all clinical personnel as indicated.</li>
</ol>
<ol start="5">
<li>Assists the Executive Director/Administrator in the preparation and administration of the organization's budget.</li>
</ol>
<ol start="6">
<li>Interprets operational indicators to detect census changes and increases or decreases in volume, which could impact staffing levels, revenues or expenses.</li>
</ol>
<ol start="7">
<li>Evaluates performance of Clinical Supervisors/Nursing Supervisors.</li>
</ol>
<ol start="8">
<li>Assists Clinical Supervisors/Nursing Supervisors to develop skills and techniques in evaluating the performance of clinicians.</li>
</ol>
<ol start="9">
<li>Hires, evaluates, and terminates organization personnel.</li>
</ol>
<ol start="10">
<li>Conducts clinician performance evaluations annually, or more frequently if indicated.</li>
</ol>
<ol start="11">
<li>Oversees the maintenance of patient clinical records, statistics, reports and records for purposes of evaluation and reporting of organization activities.</li>
</ol>
<ol start="12">
<li>Assures proper mainte&shy;nance of clinical records in compliance with local, state and federal laws.</li>
</ol>
<ol start="13">
<li>Responsible for the maintenance of adequate and appropriate inventory supplies and equipment for the provision of patient services.</li>
</ol>
<ol start="14">
<li>Develops, implements and evaluates the orientation program for new organization personnel. Responsible for orientation of new organization personnel, either directly or by delegating to another staff member.</li>
</ol>
<ol start="15">
<li>Plans and implements in-service and continuing education programs to meet education and training needs of organization personnel.</li>
</ol>
<ol start="16">
<li>Assists with the evaluation of organization performance via performance improvement program, productivity, quarterly and annual reviews. Assures for the quality and safe delivery of home health services provided through the Organization.</li>
</ol>
<ol start="17">
<li>Assists in the development of organization goals. Develops, recommends, and administers Organization policies and procedures.</li>
</ol>
<ol start="18">
<li>Assures compliance with all local, state and federal laws regarding licensure and certification of organization personnel and, maintains compliance to the Joint Commission Home Care standards.</li>
</ol>
<ol start="19">
<li>Stays informed about changes in the field of nursing and home health care; shares information with appropriate organization personnel.</li>
</ol>
<ol start="20">
<li>Promotes home health referrals in the health care community.</li>
</ol>
<ol start="21">
<li>In the absence of the Executive Director/Administrator, the Clinical Director will become the acting Executive Director/Administrator and will be vested with authority to act in behalf of the Executive Director/Administrator.</li>
</ol>
<ol start="22">
<li>Other duties as assigned by the Executive Director/Administrator.</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>Position Qualifications </strong></p>
<p>&nbsp;</p>
<ol>
<li>Registered nurse with current licensure to practice professional nursing in the State.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Bachelor&rsquo;s degree in Nursing from an accredited program by the National League for Nursing. Master&rsquo;s degree in health care preferred.</li>
</ol>
<p>&nbsp;</p>
<ol style="list-style-type: upper-alpha;">
<li>The Clinical Director/Director of Patient Care Services shall qualify for the position by fulfilling the requirements under one of the following categories, unless the individual has been previously approved for such employment by a program flexibility issued for the individual&rsquo;s current position at the hospice agency prior to April 1, 1995 or:</li>
<li>Three years of management experience within the last five years in a home health or related health care organization, at least one year of which was a supervisory or administrative capacity.</li>
<li>A registered nurse with four years of experience within the last five years in a home health agency, primary care clinic or health facility, at least one year of which was in a supervisory or administrative capacity.</li>
</ol>
<p><strong>&nbsp;</strong></p>
<ol start="3">
<li>Demonstrated ability to supervise and direct professional administrative personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Knowledge and expertise in clinical decision-making and Quality Management review and evaluation.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Ability to market and deal tactfully with customers and the community.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Must be a licensed driver with an automobile that is insured in accordance with state and/or Organization requirements and is in good working order.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Has excellent observation, verbal and written communication skills.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Knowledge of business management, governmental regulations and Joint Commission standards.</li>
</ol>
