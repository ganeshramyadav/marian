<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Information Systems Director</em></p>

<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Executive Director/Administrator</em></p>
<p><strong>Job Description Summary</strong></p>
<p>Responsible for planning, coordinating, managing and directing all information systems including financial systems, patient care systems, and general office systems</p>
<h3><strong>Essential Job Functions/Responsibilities</strong></h3>
<ol>
	<li>Directs all daily information system operations including providing direct oversight of the establishment and implementation of information system policies.</li>
	<li>Directs the implementation of improved work methods and procedures to ensure that information systems maintain functional integrity and operate at acceptable performance levels.</li>
	<li>Protects the confidentiality of information contained within the information systems through effective user controls, system auditing, and direct supervision of information systems operations. Assures that HIPAA security standards are incorporated into procedures.</li>
	<li>Protects organization assets and ensures proper utilization of organization resources by securing software and hardware from unauthorized use and theft, and through the protection of copyright agreements.</li>
	<li>Protects the information contained within all information systems through the implementation of backup systems designed to provide duplicate data.</li>
	<li>Ensures information system integration and seamless system functioning that allows flow of data between software applications.</li>
	<li>Works with the management team in identifying information system needs and develops an information system enhancement plan designed to meet those needs.</li>
	<li>Evaluates proposed information system vendor contracts and licensing agreements to minimize the organization&rsquo;s risk and to optimize financial viability of the agreements.</li>
	<li>Assists the Executive Director/Administrator in the preparation of the information system annual budget and monitors allocation of funds within budgetary limitations.</li>
	<li>Provides leadership in strategic planning including identifying opportunities for improved information systems capabilities.</li>
	<li>Maintains comprehensive working knowledge in the field of information systems and serves as a resource for organization information system users</li>
	<li>Serves as the primary liaison with information system vendors technical support teams.</li>
	<li>Assists the Executive Director and management team members in software selections.</li>
	<li>Manages other office technical systems including telecommunication systems and general office equipment.</li>
	<li>Recruits, selects, and orients new personnel and arranges for in-service education, as appropriate.</li>
</ol>
<p>The above statements are intended to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job-related tasks other than those stated in this description.</p>
<p><strong>Position Qualifications</strong></p>
<ol>
	<li>Bachelor's degree in Computer Science, Computer Engineering or related field, Master&rsquo;s Degree preferred.</li>
	<li>At least three to five years of experience in health care information systems management, preferably in home care operations.</li>
	<li>Demonstrated ability to supervise and direct professional and administrative personnel.</li>
	<li>Knowledge of corporate business management.</li>
	<li>Demonstrates good communications and public relations skills.</li>
</ol>
