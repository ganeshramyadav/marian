<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Infusion Therapy Nurse Coordinator</p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Director</em></p>
<h3><strong>Job Description Summary</strong></h3>
<p>The Infusion Therapy Coordinator is responsible to oversee home infusion therapy services and develop professional relationships with physicians, pharmacists, home infusion nurses and health care facility discharge planners.</p>
<h3><strong>Essential Job Functions/Responsibilities</strong></h3>
<ol>
	<li>Collaborates with home health team to identify potential patients for home infusion therapy.</li>
	<li>Performs initial assessment to determine eligibility for home infusion therapy.</li>
	<li>Assigns clinician to make home visit within 24 hours of referral to assess the home environment and initiate services.</li>
	<li>Coordinates patient care between pharmacist, clinicians, and physician regarding initial drug therapy, any drug therapy problems, and changes in administration.</li>
	<li>Facilitates communication between patient, family, and caregiver, clinicians, pharmacist, physician, and delivery personnel regarding the supplying, resupplying, and pick-up delivery schedule.</li>
	<li>Makes visits to organization patients who have been hospitalized to keep abreast of condition and impending discharge.</li>
	<li>Keeps physicians, hospital discharge planners, and organization personnel, as appropriate, informed of home care patient progress following hospital discharge.</li>
	<li>Responds to billing department needs and confirms that appropriate documentation is available, regarding home infusion therapy.</li>
	<li>Performs direct patient care, as needed, in an efficient, effective, and clinically correct manner.</li>
	<li>Conducts inservice programs regarding home infusion therapy services and specific infusion skills for nurses.</li>
	<li>Provides portions of orientation and training for new organization personnel regarding home infusion therapy services.</li>
	<li>Serves as a resource for nurses and pharmacists regarding information about home infusion services.</li>
	<li>Provides input to assist the Clinical Director and Clinical Supervisors/Nursing Supervisors in the evaluation of organization personnel performance.</li>
	<li>Adheres to professional and ethical standards of nursing practice.</li>
	<li>Performs other duties as assigned.</li>
</ol>
<p>The above statements are only meant to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job related tasks other than those stated in this description.</p>
<h3><strong>Position Qualifications</strong></h3>
<ol>
	<li>Registered nurse with current licensure to practice professional nursing in the State.</li>
	<li>Graduate of National League for Nursing Accredited School of Nursing. BSN preferred.</li>
	<li>Minimum of two years of experience in home health nursing.</li>
	<li>Professional certification by Infusion Nursing Society.</li>
	<li>Possesses and maintains current CPR certification.</li>
	<li>Must be a licensed driver with an automobile that is insured in accordance with state/or organization requirements and is in good working order.</li>
</ol>
