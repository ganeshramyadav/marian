<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>OASIS Review Nurse</em></p>
<p><strong>Reports to:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Performance Improvement Coordinator</em></p>
<p><strong>Job Description Summary </strong></p>
<p>The OASIS Review Nurse is a professional, registered nurse responsible for analyzing data integrity and consistency of OASIS documentation and assessment processes.&nbsp; This position will ensure appropriate ICD-9 coding and sequencing and will work with clinical staff to clarify documentation and data integrity issues.</p>
<p><strong>Essential Job Functions/Responsibilities</strong></p>
<ol>
	<li>Prospectively reviews all OASIS assessments to ensure appropriateness, completeness, and compliance with federal and state regulations and organization policy.</li>
	<li>Utilizes OASIS variation or alert reports when reviewing OASIS data.</li>
	<li>Ensures appropriate ICD-9 coding and sequencing as it relates to the patient&rsquo;s medical condition, including any co-morbidities.</li>
	<li>Consults with appropriate clinical staff to clarify any data integrity issues and works with clinician to make appropriate corrections per organization policy.</li>
	<li>Reviews visit utilization for appropriateness of care guidelines and patient condition; reports potential financial losses and/or underutilization to the clinical manager/designee.</li>
	<li>Notifies organization leadership of problematic trends as a result of OASIS review.</li>
	<li>Works with managers to address trends that affect the agency&rsquo;s outcome and process measures noted during OASIS review.</li>
	<li>Makes OASIS inter-rater reliability visits with clinical staff.</li>
	<li>Participates in Quality Improvement and Corporate Compliance activities as assigned.</li>
	<li>Assists with other chart audit activities as assigned.</li>
	<li>Maintains professional and technical knowledge by attending educational workshops and reviewing professional publications.</li>
</ol>
<p>The above statements are only meant to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job related tasks other than those stated in this description.</p>	
<p><strong>Position Qualifications</strong></p>
<ol>
	<li>Registered nurse with current license to practice professional nursing in the state.</li>
	<li>One (1) to two (2) years of clinical home health experience required.</li>
	<li>Working knowledge of OASIS and ICD-9 coding. OASIS certification (COS-C) preferred; OASIS Home health ICD-9 coding certification preferred.</li>
	<li>Knowledge of federal regulations and state licensure requirements.</li>
	<li>Working knowledge of computer systems.</li>
	<li>Possess and maintains current CPR certification.</li>
	<li>Licensed driver with automobile that is in good working order and insured in accordance with organization requirements.</li>
	<li>Excellent coordination and communication skills.</li>
	<li>Detail oriented and able to work with minimal supervision.</li>
</ol>
