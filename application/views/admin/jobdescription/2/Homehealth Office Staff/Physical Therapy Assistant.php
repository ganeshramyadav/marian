<p style="margin-top: 0.08in; margin-bottom: 0in; line-height: 100%"><a name="PhysicalTherapyassistant"></a>
<font face="Times New Roman, serif"><b>Job Title/Position:</b></font><font face="Times New Roman, serif">	</font><font face="Times New Roman, serif"><font size="3"><i>Physical
Therapy Assistant</i></font></font></p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif"><b>Reports
To:</b></font><font face="Times New Roman, serif">		</font><font face="Times New Roman, serif"><font size="3"><i>Physical
Therapist or Rehab Supervisor</i></font></font></p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Job
Description Summary</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">The
physical therapy assistant provides physical therapy services under
the direction of the physical therapist and according to the
physician's plan of care.</font></p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Essential
Job Functions/Responsibilities</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Provides
	direct physical therapy according to directions of physical
	therapist and in accordance with the physician's plan of care.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Treats
	patient to relieve pain, develop or restore function, and maintain
	maximum performance.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Directs
	and aids patients in active and passive therapeutic exercises,
	muscle reeducation, gait and functional training, transfer
	activities and prosthetic training.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="4">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Utilizes
	physical agents, including, but not be limited to heat, cold, water,
	light, electricity, ultrasound and massage.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="5">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Observes
	and reports to the physical therapist and the Organization personnel
	the patient’s reaction to treatment and any changes in
	patient’s condition.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="6">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Instructs
	patients in care and use of wheelchairs, braces, crutches, canes,
	and prosthetic or orthotic devices.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="7">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Maintains
	necessary records; clinical notes and conference notes, which will
	be incorporated into the patient’s clinical record in
	prescribed time frames. </font></font>
	</p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="8">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Participates
	in Organization Home Care Team Members meetings and Organization
	Inservice Education Program.</font><font face="Times New Roman, serif"><b>	</b></font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="9">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Performs
	other duties as assigned.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Position
Qualifications</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Graduate
	of an accredited physical therapy assistant program with either an
	Associate’s degree in science or applied science that is
	approved by American Physical Therapy Association, Inc.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Possesses
	and maintains current CPR certification.</font></font></p>
</li></ol>
<p style="margin-left: 0.3in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Current
	certification or license as required by state law.</font></font></p>
</li></ol>
