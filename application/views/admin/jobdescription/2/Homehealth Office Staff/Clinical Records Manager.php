<p><strong>Job Title/Position:</strong><em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </em><em>Clinical Records Manager</em></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Director</em></p>
<p><strong>Job Description Summary</strong></p>
<p>Responsible for managing all aspects of maintaining and protecting organization clinical records including the management of the clinical records team.</p>
<h3><strong>Essential Job Functions/Responsibilities</strong></h3>
<ol>
	<li>Manages all daily clinical records functions including establishing and implementing clinical records policies.</li>
	<li>Ensures clinical record systems are maintained in compliance with state, federal and Joint Commission regulations.</li>
	<li>Maintains comprehensive working knowledge of state, federal, and Joint Commission documentation regulations and serves as a resource for appropriate organization personnel.</li>
	<li>Protects all clinical records through the establishment and implementation of control procedures for all open and closed records.</li>
	<li>Protects the confidentiality of patient and organization information through effective controls and direct supervision of clinical records.</li>
	<li>Forwards copies of clinical records to authorized users according to policy.</li>
	<li>Completes an administrative record audit following patient discharge and forwards the record to the Clinical Supervisor/Nursing Supervisor for clinical audit.</li>
	<li>Monitors aged unsigned physician orders and submits second and third requests for signed orders to overdue physicians, calls or visits the offices of seriously overdue physicians.</li>
	<li>Supervises the use of the clinical records information system and maintains a comprehensive working knowledge of the system including upgrades and enhancements.</li>
	<li>Recruits, selects, orients, and manages members of the clinical records team.</li>
	<li>Manages the members of the clinical record team to ensure accurate and prompt filing and retrieval of clinical records.</li>
</ol>
<p>The above statements are intended to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job-related tasks other than those stated in this description.</p>
<p><strong>Position Qualifications</strong></p>
<ol>
	<li>An Accredited Records Technician Degree, Bachelor&rsquo;s degree preferred.</li>
	<li>At least three years of experience in clinical records management preferably in home health care operations. Information systems knowledge required.</li>
	<li>Demonstrated ability to supervise and direct clerical personnel.</li>
	<li>Knowledge of corporate business management, governmental regulations and Joint Commission standards.</li>
	<li>Demonstrates good communication and public relations skills.</li>
	<li>Demonstrates autonomy, organization, assertiveness, flexibility and cooperation in performing job responsibilities.</li>
</ol>
