<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Managed Care Coordinator</em></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Director</em></p>
<h3><strong>Job Description Summary</strong></h3>
<p>Responsible for coordinating home care services for managed care patients, which includes establishing and maintaining the patient&rsquo;s plan of care, establishing and maintaining positive relationships with managed care customers and related referral sources, and negotiating service agreements.</p>
<h3><strong>Essential Job Functions/Responsibilities</strong></h3>
<ol>
	<li>Coordinates home care services for managed care patients, assists with the establishment of the plan of care in coordination with the physician and insurance Case Managers, and ensures a seamless transition to home care.</li>
	<li>Collaborates with inpatient utilization review departments and managed care Case Managers to facilitate safe, early inpatient discharge.</li>
	<li>Verifies insurance coverage and obtains initial authorizations for services and seeks reauthorization for ongoing and additional services.</li>
	<li>Works with Clinical Supervisors/Nursing Supervisors and staff clinicians to gather, collate, and report patient care summary information to managed care Case Managers.</li>
	<li>Promotes maximum third party reimbursement through price negotiations with managed care Case Managers, within the established financial, credit, and contractual parameters.</li>
	<li>Maintains comprehensive working knowledge of managed care contractual relationships and ensures that patients are admitted and services are delivered in accordance with contract provisions.</li>
	<li>Works with members of the billing team to ensure that managed care customers are billed according to Case Manager Agreements.</li>
	<li>Establishes and maintains positive working relationships with current and potential managed care customers.</li>
	<li>Promptly responds to managed care customer&rsquo;s requests and concerns.</li>
	<li>Assists in building community, customer, payer and patient perceptions of Lifelink Health Providers, Inc. as a high quality provider of services.</li>
	<li>Maintains comprehensive working knowledge in the field of managed care and shares information with appropriate organization personnel.</li>
	<li>Maintains comprehensive working knowledge of community resources and assists customers in accessing community resources should services not be provided by Lifelink Health Providers, Inc.</li>
</ol>
<p>The above statements are intended to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job-related tasks other than those stated in this description.</p>
<h3><strong>Position Qualifications</strong></h3>
<ol>
	<li>Registered nurse with current licensure to practice professional nursing in the state.</li>
	<li>Graduate of National League for Nursing Accredited School of Nursing. BSN degree is preferred.</li>
	<li>At least three years of experience in health care managed care coordination, preferably in home health care.</li>
	<li>Complies with acceptable professional standards and practice.</li>
	<li>Ability to market aggressively and deal tactfully with customers and the community.</li>
	<li>Knowledge of corporate business management and managed care environment.</li>
	<li>Demonstrates good communication, negotiation, and public relations skills.</li>
	<li>Demonstrates autonomy, organization, assertiveness, flexibility, and cooperation in performing job responsibilities.</li>
</ol>
