<p style="margin-top: 0.08in; margin-bottom: 0in; line-height: 100%"><a name="PhysicalTherapist"></a>
<font face="Times New Roman, serif"><b>Job Title/Position:</b></font><font face="Times New Roman, serif">	</font><font face="Times New Roman, serif"><font size="3"><i>Physical
Therapist</i></font></font></p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif"><b>Reports
To:</b></font><font face="Times New Roman, serif">		</font><font face="Times New Roman, serif"><font size="3"><i>Clinical
Supervisor/Nursing Supervisor</i></font></font></p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Job
Description Summary</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">The
physical therapist contracted or employed through the Organization is
responsible for providing physical therapy services and for adherence
to all conditions outlined in the Personnel Services Agreement.</font></p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Essential
Job Functions/Responsibilities</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Provides
	physical therapy services to patients according to a written
	physician's plan of care.  This may include, but will not be limited
	to:</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<ol type="A">
		<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">Assessing
		and evaluating therapeutic/rehabilitative/functional status, and
		participation in the development of the total plan of care. 
		Evaluates home environment.</font></p>
	</li></ol>
</ol>
<p style="margin-left: 0.3in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<ol type="A" start="2">
		<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">Assesses
		for muscle strength, mobility, gait, ROM—potential for rehab.</font></p>
	</li></ol>
</ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<ol type="A" start="3">
		<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">Directing
		physical therapy treatment.</font></p>
	</li></ol>
</ol>
<p style="margin-left: 0.3in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<ol type="A" start="4">
		<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">Instructing
		patients and families/caregivers in the use and care of therapeutic
		appliances.</font></p>
	</li></ol>
</ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<ol type="A" start="5">
		<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">Determining
		priority needs for physical therapy</font></p>
	</li></ol>
</ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<ol type="A" start="6">
		<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">Reporting
		to physician patient’s reaction to treatment or changes in
		condition.</font></p>
	</li></ol>
</ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Initiates
	physical therapy program and instructs other personnel and/or
	family/caregiver members in certain phases of physical therapy with
	which they may work with a patient, as well as instructing them as
	to the goals of the physical therapy program for the patient by
	participating in case conferences.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">May
	train patient in the use of prosthetic device. </font></font>
	</p>
</li></ol>
<p style="margin-left: 0.5in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="4">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Identifies
	patient and family/caregiver needs for other home health services
	and refers <br>as necessary.</font></font></p>
</li></ol>
<p style="margin-left: 0.3in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="5">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Prepares
	and submits clinical and progress summaries based on the attainment
	of goals.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="6">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Participates
	in discharge planning for patient.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="7">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Prepares
	and submits a clinical progress summary based on the attainment of
	goals as directed by Organization policy.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="8">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Provides
	physical therapy consultation to home families/caregivers when
	indicated.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="9">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Provides
	inservice education programs for nursing organization personnel as
	needed.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="10">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Participates
	in peer consultation process.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="11">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Supervises
	Physical Therapy Assistants according to organization policy and
	state regulations.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="12">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">When
	therapy is the only skilled service, instructs, supervises and
	evaluates home health aide care every two (2) weeks.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">The
above statements are intended to be a representative summary of the
major duties and responsibilities performed by incumbents of this
job.  The incumbents may be requested to perform job related tasks
other than those stated in this description.</font></p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Position
Qualifications</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Possesses
	a degree from a baccalaureate or masters program in physical therapy
	approved by an accredited organization.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Licensed
	to practice as physical therapist within the State, if applicable.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Two
	years of appropriate experience as a physical therapist. 
	Community/home health experience is preferred.</font></font></p>
</li></ol>
<p style="margin-left: 0.5in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-left: 0.3in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="4">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Demonstrates
	good verbal and written communication and organization skills.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="5">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Possesses
	and maintains current CPR certification.</font></font></p>
</li></ol>
<p style="margin-left: 0.5in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="6">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Must
	be a licensed driver with an automobile that is insured in
	accordance with state/or organization requirements and is in good
	working order.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
