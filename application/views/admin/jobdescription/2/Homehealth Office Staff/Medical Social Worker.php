<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Position
Qualifications</b></font></font></h3>
<br/>
<ol>
	<li>Graduate of a bachelor’s program in social work accredited by the Council on Social Work Education.</li>
	<li>Licensed as a licensed clinical social worker by the Board of Behavioral Sciences.</li>
	<li>Minimum of one year's experience in health care.</li>
	<li>Experience in a home health care preferred.</li>
	<li>Demonstrates good verbal and written communication, and organization skills.</li>
	<li>Possesses and maintains current CPR certification.</li>
	<li>Must be a licensed driver with an automobile that is insured in accordance with state/or organization requirements and is in good working order.</li>
</ol>

