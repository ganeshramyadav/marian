<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Human Resources Director</em></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Executive Director/Administrator</em></p>
<p><strong>Job Description Summary</strong></p>
<p>Responsible for planning, coordinating, managing and directing all activities and programs relating to the recruitment, selection, retention, orientation and development of the Lifelink Health Providers,&nbsp; Inc. employees.&nbsp; Acts as an advocate for organization personnel and collaborates with other Directors in Human Resources issues.</p>
<p><strong>Essential Job Functions/Responsibilities</strong></p>
<ol>
	<li>Directs all daily human resources operations including providing direct oversight of the establishment and implementation of human resource policies.</li>
	<li>Ensurescompliancewithallstate, federal, andJoint Commission humanresourcesregulatoryrequirements.</li>
	<li>Directs the implementation of improved work methods and procedures to ensure that consistent employee recruitment, selection, retention, hiring practices, orientation and termination procedures are handled in accordance with applicable law and regulation.</li>
	<li>Monitors competitor&rsquo;s wage, salary and benefit structures and makes recommendations to the Executive Director for compensation adjustments to ensure that the Lifelink Health Providers, is competitive in the market.</li>
	<li>Works with the management team to identify recruitment needs and develops a recruitment plan designed to fill demand, and evaluates the cost-effectiveness of recruitment efforts.</li>
	<li>Builds and monitors community perceptions of Lifelink Health Providers, as a high-quality employer.</li>
	<li>Assists the Executive Director/Administrator in the preparation of the annual budget for the human resources department and monitors the allocation of resources according to budgetary limitations.</li>
	<li>Coordinates and participates in community awareness and recruitment activities such as career fairs.</li>
	<li>Works with the management team in identifying employee education needs and develops orientation and continuing education programs for all employees.</li>
	<li>Recruits, selects, and directly manages members of the human resource team.</li>
	<li>Maintains a comprehensive library of resources to support employee education and development.</li>
	<li>Maintains confidentiality of all employee information and files.</li>
	<li>Annually monitors personnel satisfaction with benefits and work environment and reports the results per organization practice.</li>
	<li>Makes recommendations regarding organization personnel benefits package, and cost effective plans.</li>
</ol>
<p>The above statements are intended to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job-related tasks other than those stated in this description.</p>
<h3><strong>Position Qualifications</strong></h3>
<ol>
	<li>Bachelor's degree in Business Administration or related field, Master&rsquo;s Degree preferred.</li>
	<li>At least three years of experience in health care management preferably in home care operations. Two years of experience in employee recruitment and training preferred.</li>
	<li>Knowledge of state, federal and Joint Commission regulations relating to employment and  employee education.</li>
	<li>Knowledge of corporate business management.</li>
	<li>Demonstrates good verbal and written communication and public relations skills.</li>
	<li>Demonstrates autonomy, organization, assertiveness, flexibility, and cooperation in performing job responsibilities.</li>
</ol>
