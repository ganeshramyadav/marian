Description Summary</b></font></font></h3>

<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">The
Licensed Practical/Vocational Nurse is responsible for providing
direct patient care under the supervision of a registered nurse. 
Responsibilities include following the plan of care, providing
treatments, and working collaboratively with the members of the team
to help meet positive patient care outcomes.</font></p>
<br/>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Essential
Job Functions/Responsibilities</b></font></font></h3>
<ol>
	<li>Providedirect patient care as defined in State’s Name Nurse Practice Act.</li>
	<li>Implements plan of care initiated by the registered nurse.</li>
	<li>Provides accurate and timely documentation consistent with the plan of care.</li>
	<li>Assesses and provides patient and family/caregiver education and information pertinent to diagnosis and plan of care.</li>
	<li>Participates in coordination of home health services, in coordination of home health services, appropriately reporting the identified needs for other disciplines (HHA, OT, PT, MSW, ST,Dietician) to the registered nurse and/or Clinical
	Supervisor/Nursing Supervisor.</li>
	<li>Uses equipment and supplies effectively and efficiently.</li>
	<li>Participates  in personal and professional growth and development.</li>
	<li>Performs other duties as assigned by the registered nurse.</li>	
</ol>
