<p><strong>Job Title/Position: </strong><em>Medical Director</em></p>
<p><strong>PURPOSE</strong></p>
<p>To outline the responsibilities of the Medical Director.</p>
<p><strong>POLICY</strong></p>
<p>Lifelink Providers, Inc. will have a written service agreement with a qualified physician to provide medical direction and consultation for organization programs and services.&nbsp;</p>
<p><strong>The duties and responsibilities of the Medical Director will include, but not be limited to, the following:</strong></p>
<ol>
	<li>Devote his/her best ability to the proper management of the program.</li>
	<li>Provide overall medical direction to the program.</li>
	<li>Assure that established policies, bylaws, rules, and regulations of the organization are followed in the program.</li>
	<li>Adhere to requirements, terms, and conditions required by Medicare Conditions of Participation, Joint Commission, and federal and state statures governing the provision of services.</li>
	<li>Establish and continually review policies and procedures related to patient care, medical education, and emergency procedures.</li>
	<li>Develop and continually review, in cooperation with the Executive Director/Administrator and/or Clinical Director, criteria to monitor the quality of the education programs provided to physicians, personnel, and volunteers.</li>
	<li>Evaluate quality assurance and performance improvement plans and monitor to identify medical education needs in cooperation with the Executive Director/Administrator and/or Clinical Director.</li>
	<li>Propose organization programs to address the needs identified (with the assistance and input of consultants of the specialties where medical education needs were identified).</li>
	<li>Work with the Executive Director/Administrator and/or Clinical Director, after implementation of the programs, to determine the impact of said programs on the quality of care.</li>
	<li>Conduct periodic inservice education programs for department personnel.</li>
	<li>Assist with evaluation of equipment purchases.</li>
	<li>Perform in a cost-effective manner with respect to utilization of organization resources.</li>
</ol>
<p>I have read the above job description and understand the duties and responsibilities associated with the position. I can perform the essential functions of this position without specific accommodations.</p>
