<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Position
Qualifications</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Possesses
	a master’s or doctoral degree in speech-language pathology
	(SLP), and who meets either of the following requirements:</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol type="A">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Is
	licensed as a speech language pathologist by the State in which the
	individual furnishes such service; or</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol type="A" start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">If
	the State does not license speech-language pathologists:</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Must
	successfully completed 350 clock hours of supervised clinical
	practicum (or is in the process of accumulating such supervised
	clinical experience);</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Performed
	not less than 9 months of supervised full-time SLP services after
	obtaining a master’s or doctoral degree in SLP or a related
	field; and</font></font></p>
</li></ol>
<p style="margin-left: 0.5in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Successfully
	completed an approved national examination in SLP</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">A
	minimum of one year's clinical experience in Speech–Language
	Pathology.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Demonstrates
	good verbal and written communication, and organization skills.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="4">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Possesses
	and maintains current CPR certification.</font></font></p>
</li></ol>
<p style="margin-left: 0.5in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="5">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Must
	be a licensed driver with an automobile that is insured in
	accordance with state/or organization requirements and is in good
	working order.</font></font></p>
</li></ol>
