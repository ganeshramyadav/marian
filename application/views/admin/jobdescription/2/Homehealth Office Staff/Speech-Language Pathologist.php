<p style="margin-top: 0.08in; margin-bottom: 0in; line-height: 100%"><a name="SpeechLanguagePathologist"></a>
<font face="Times New Roman, serif"><b>Job
Title/Position:</b></font><font face="Times New Roman, serif">	</font><font face="Times New Roman, serif"><font size="3"><i>Speech-Language
Pathologist</i></font></font></p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif"><b>Reports
To:</b></font><font face="Times New Roman, serif">		</font><font face="Times New Roman, serif"><font size="3"><i>Clinical
Supervisor/Nursing Supervisor</i></font></font></p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Job
Description Summary</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">The
speech–language pathologist is responsible for implementation
of standards of care for speech language pathology services and for
adherence to all conditions in the Service Agreement.</font></p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Essential
Job Functions/Responsibilities</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Conducts
	appropriate evaluations, assesses home environment (as appropriate),
	identifies equipment needs relative to speech/language function.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Identifies
	functional speech defects and establishes a plan of care to improve
	patient’s function. </font></font>
	</p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Administers
	speech therapy program utilizing specialized therapeutic technique
	and/or equipment.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="4">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Recommends
	communication devices/aids as indicated.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="5">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Confers
	with referring physician and other organization personnel to ensure
	coordinated and comprehensive care contributes to the total plan of
	care.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="6">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">As
	appropriate, participates in case conferences, inservices and other
	meetings required to ensure coordinated and comprehensive care.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="7">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Prepares
	and submits notes per organization policy.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="8">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Maintains
	clinical competency in speech language pathology practice and
	theory.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="9">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Identifies
	patient and family/caregiver needs for other services and refers as
	appropriate.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="10">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Instructs
	patient, family/caregiver and other Organization health care
	personnel in the patient’s treatment regime as indicated.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="11">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">May
	supervise home health aide in procedures directly related to the
	speech–language therapy plan of care.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="12">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Performs
	other duties as required to facilitate the delivery of speech
	language <br>pathology services.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">The
above statements are only meant to be a representative summary of the
major duties and responsibilities performed by incumbents of this
job.  The incumbents may be requested to perform job related tasks
other than those stated in this description.</font></p>
