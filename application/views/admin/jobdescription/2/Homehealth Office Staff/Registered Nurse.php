<h4 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif">Communication</font></font></h4>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Prepares
	clinical notes and updates the primary physician when necessary and
	at least every sixty days.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Communicates
	with the physician regarding the patient’s needs and reports
	any changes in the patient’s condition; obtains/receives
	physician’s orders as required.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Communicates
	with community health related persons to coordinate the care plan.</font></font></p>
</li></ol>
<h4 class="western" style="font-weight: normal; line-height: 100%"></h4>
<h4 class="western" style="font-weight: normal; line-height: 100%"></h4>
<h4 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif">Additional
Duties</font></font></h4>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Participates
	in on-call duties as defined by the on-call policy.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Ensures
	that arrangements for equipment and other necessary items and
	services are available.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Instructs,
	supervises and evaluates home health aide care provided every two
	(2) weeks.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
