<p style="margin-top: 0.08in; margin-bottom: 0in; line-height: 100%"><a name="RegisteredNurse"></a>
<font face="Times New Roman, serif"><b>Job Title/Position:</b></font><font face="Times New Roman, serif">	</font><font face="Times New Roman, serif"><font size="3"><i>Registered
Nurse (RN)</i></font></font></p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif"><b>Reports
To:</b></font><font face="Times New Roman, serif">		</font><font face="Times New Roman, serif"><font size="3"><i>Clinical
Supervisor/Nursing Supervisor</i></font></font></p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Job
Description Summary</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Times New Roman, serif">The
registered nurse plans, organizes and directs home care services and
is experienced in nursing, with emphasis on community health
education/experience.  The professional nurse builds from the
resources of the community to plan and direct services to meet the
needs of individuals and families within their homes and communities.</font></p>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Essential
Job Functions/Responsibilities</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<h4 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif">Patient
Care</font></font></h4>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Completes
	an initial assessment of patient and family to determine home care
	needs.  Provides a complete physical assessment and history of
	current and previous illness(es).</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Regularly
	re-evaluates patient nursing needs.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Initiates
	the plan of care and makes necessary revisions as patient status and
	needs change.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="4">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Uses
	health assessment data to determine nursing diagnosis.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="5">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Develops
	a care plan, which establishes goals based on nursing diagnosis and
	incorporates therapeutic, preventive, and rehabilitative nursing
	actions.  Includes the patient and the family in the planning
	process.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="6">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Initiates
	appropriate preventive and rehabilitative nursing procedures. 
	Administers medications and treatments as prescribed by the
	physician.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="7">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Counsels
	the patient and family in meeting nursing and related needs.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="8">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Provides
	health care instructions to the patient as appropriate per
	assessment and plan of care.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="9">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Identifies
	discharge planning needs as part of the care plan development and
	implements prior to discharge of the patient.</font></font></p>
</li></ol>
<p style="margin-left: 0.5in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<p style="margin-left: 0.3in; margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="10">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Acts
	as Case Manager when assigned by Clinical Supervisor/Nursing
	Supervisor and assumes responsibility to coordinate patient care for
	assigned caseload.</font></font></p>
</li></ol>
