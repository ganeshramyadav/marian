<h3 class="western" style="line-height: 100%"><font color="#00000a"><font face="Times New Roman, serif"><b>Position
Qualifications</b></font></font></h3>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol>
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Registered
	nurse with current licensure to practice professional nursing in the
	state.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="2">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Graduate
	of National League for Nursing Accredited School of Nursing.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="3">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Maintains
	a current CPR certification.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="4">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Must
	be a licensed driver with an automobile that is insured in
	accordance with state/or organization requirements and is in good
	working order.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="5">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Minimum
	of two years of experience, at least one of which is in the area of
	public health or home care nursing is preferred.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="6">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Self-directed
	and able to work with minimal supervision.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="7">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Demonstrates
	excellent observation, problem solving, verbal and written
	communication skills; nursing skills per competency checklist.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="8">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Shows
	ability to organize and prioritize workload independently.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="9">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Management
	experience not required.  Responsible for supervising home health
	aides.</font></font></p>
</li></ol>
<p style="margin-bottom: 0in; line-height: 100%"><br>
</p>
<ol start="10">
	<li><p style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><font face="Times New Roman, serif">Prolonged
	or considerable walking or standing.  Able to lift, position, or
	transfer patients.  Able to lift supplies and equipment. 
	Considerable reaching, stooping, bending, kneeling, or crouching. 
	Visual acuity and hearing to perform required nursing skills.</font></font></p>
</li></ol>
