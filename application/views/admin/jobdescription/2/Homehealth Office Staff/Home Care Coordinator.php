<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Home Care Coordinator</p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Clinical Supervisor/Nursing Supervisor</p>
<p><strong>Job Description Summary</strong></p>
<p>Evaluates patients referred to the Organization; identifies insurance coverage, negotiates payment or per diem rates; plans and coordinates referrals for home care services.</p>
<h3><strong>Essential Job Functions/Responsibilities</strong></h3>
<ol>
	<li>Evaluates hospitalized patients referred for home care.</li>
	<li>Identifies and verifies insurance coverage of home health care services.</li>
	<li>Obtains and documents prior authorization for home care services from insurance providers.</li>
	<li>Negotiates per diem or per visit payment rates for creative bundling of home care services.</li>
	<li>Collaborates with the hospital's discharge planning personnel, utilization review department and insurance Case Managers to facilitate safe, early discharge.</li>
	<li>Consults with physicians, nurses, social workers, discharge planners and other disciplines to establish a coordinated home plan of care.</li>
	<li>Interviews the patient, family, and caregiver and discusses the home situation, current needs, and any psychosocial factors that are relevant to the plan of care.</li>
	<li>Maintains liaison relationship with hospital and insurance personnel, providing information and education on Organization services, coverage issues and related areas.</li>
	<li>Participates in patient care conferences and inservices.</li>
	<li>Collects and maintains statistical data on all referrals and submits them regularly as required.</li>
	<li>Attends meetings and participates on committees as required.</li>
	<li>Performs other duties as assigned.</li>
</ol>
<p>The above statements are only meant to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job related tasks other than those stated in this description.</p>
<p><strong>Position Qualifications</strong></p>
<ol>
	<li>Registered nurse with current licensure to practice professional nursing in the State.</li>
	<li>Graduate of National League for Nursing Accredited School of Nursing.</li>
	<li>Maintains a current CPR certification.</li>
	<li>Minimum of two years of experience, at least one of which is in the area of public health or home care nursing.</li>
	<li>Demonstrates an ability to work with other health care clinicians, development of home plan of care, knowledge of third party reimbursement, and ability to negotiate payment rates.</li>
	<li>Self-directed and able to work with minimal supervision.</li>
	<li>Demonstrate excellent observation verbal and written communication skills.</li>
</ol>
