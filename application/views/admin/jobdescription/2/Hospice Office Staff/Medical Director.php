<p><strong>JOB DESCRIPTION</strong></p>
<p><strong>Medical Director</strong></p>
<p>&nbsp;</p>
<p><strong>REPORTING RELATIONSHIP:</strong></p>
<p>&nbsp;</p>
<p>Supervised by:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Administrator and Director</p>
<p>Positions Supervised:&nbsp; Hospice IDG</p>
<p>Interrelationships:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Patients, family, IDG and other health care team members</p>
<p>&nbsp;</p>
<p><strong>JOB SUMMARY:</strong></p>
<p>Directs the medical aspects of the Hospice&rsquo;s patient care program. Serves as consultant and advisor to the Director, offering his/her expertise to assess and interpret medical problems. The Hospice physician serves as consultant to the primary physician of Hospice patients, but does not take over the medical direction of the patient&rsquo;s care. Serves as consultant to the Interdisciplinary Group.</p>
<p>&nbsp;</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>Educational/Degree:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Graduate from an accredited school of medicine and is a doctor of medicine or osteopathy.</p>
<p>&nbsp;</p>
<p>Training/Licensure:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Practicing physician currently licensed under the provisions of the State Board of Medicine.</p>
<p>&nbsp;</p>
<p>Knowledge/Skills/Ability:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Approved for admitting privileges and treatment of patients in the hospital of their locality of practice. Understanding of the principles of Hospice care. Willingness to work as a Hospice team member.</p>
<p>&nbsp;</p>
<p>Experience:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Demonstrated knowledge and well developed skills in: medicine, oncology, pharmacology; pain and symptom control; psychology of loss; and acceptance of Hospice principles of care.</p>
<p>&nbsp;</p>
<p><strong>JOB FACTORS:</strong></p>
<p>&nbsp;</p>
<p><strong><u>Physical Requirements:</u></strong></p>
<p>Must possess sight/hearing senses or use appropriate adaptive devices that will enable senses to function at a level required to meet the essential duties of the position.</p>
<p>&nbsp;</p>
<p><strong><u>Mental Requirements:</u></strong></p>
<p>Must be able to work independently, make judgments based on assessments and data available and act accordingly. Must be flexible, innovative and possess good interpersonal skills. Must be able to cope with mental and emotional stress and demonstrate emotional stability.</p>
<p>&nbsp;</p>
<p><strong><u>Working Conditions:</u></strong></p>
<p>Be able to tolerate exposure to elements including, but not limited to, odors, blood, body fluids and excrements, adverse environmental conditions and hazardous materials.</p>
<p>&nbsp;</p>
<p><strong><u>Transportation:</u></strong></p>
<p>Must have a current valid driver&rsquo;s license, auto liability insurance and reliable transportation.</p>
<p>&nbsp;</p>
<p><strong><u>Essential Functions:</u></strong></p>
<p>&nbsp;</p>
<ol>
<li>Oversees the implementation of the entire physician, nursing, social work, therapy and counseling areas within Hospice to ensure that these areas consistently meet patient and family needs.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>The Hospice Medical Director, physician employees and contracted physicians, in conjunction with the patient&rsquo;s attending physician, are responsible for the palliation and management of terminal illness and conditions related to the terminal illness.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>The Medical Director assumes overall responsibility for the medical component of the Hospice patient care program, functions as part of the Hospice IDG, and acts as consultant for medical care.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>The duties and responsibilities of the Medical Director include, but are not limited to:</li>
</ol>
<ul>
<li>Oversees the implementation of the entire physician, nursing, social work, therapy and counseling areas within Hospice to ensure that these areas consistently meet patient and family needs.</li>
<li>Responsibility for medical component of the Hospice&rsquo;s patient care program.</li>
<li>Participates in and acts as a medical resource to the IDG and Hospice leadership.</li>
<li>The Medical Director or physician designee reviews the clinical information for each Hospice patient and provides written certification that it is anticipated that the patient&rsquo;s life expectancy is 6 months or less if the illness runs its normal course. The physician must consider the following when making this determination:</li>
<li>The primary terminal condition.</li>
<li>Related diagnosis (es), if any.</li>
<li>Current subjective and objective medical findings.</li>
<li>Current medication and treatment orders.</li>
<li>Information about the medical management of any of the patient&rsquo;s conditions unrelated to the terminal illness.</li>
<li>Before the recertification period for each patient the Medical Director or physician designee must review the patient&rsquo;s clinical information.</li>
<li>Participates in the establishment and implementation of the plan of care, which is coordinated with the attending physician and IDG prior to providing care.</li>
<li>Participates in conjunction with the attending physician and IDG to review, update and sign the plan of care when changes are made and at least every fifteen days.</li>
<li>Consults with attending physicians, if requested.</li>
<li>Is available to patients on a 24-hour basis to manage their terminal illness and medical needs to the extent that the attending physician is absent or not able to meet these needs.</li>
</ul>
<p><strong><em><br /> </em></strong></p>
<p><strong><em>Job Description &ndash; Medical Director&hellip;.continued</em></strong></p>
<p>&nbsp;</p>
<ul>
<li>Acts as a liaison with other physicians in the community serviced by Hospice and facilitates communication.</li>
<li>Participates in educational programs for staff, when requested.</li>
<li>Is a member of and participates in designated interdisciplinary group activities.</li>
<li>Provides advice, guidance and assistance to Hospice staff until a satisfactory resolution is reached when a medical order:
<ul>
<li>Is of a questionable nature.</li>
<li>Contains a discrepancy.</li>
<li>Lacks clarity.</li>
<li>Continues to be a concern for the staff after consultation with the primary physician.
<ul>
<li>Provides medical consultation and direction when the attending physician or their designee cannot be reached and there is a change in the patient&rsquo;s condition requiring medical attention.</li>
</ul>
</li>
</ul>
</li>
</ul>
<p>&nbsp;</p>
<ol start="5">
<li>When the Medical Director is not available, a physician designated by Hospice assumes the same responsibilities and obligations as the Medical Director.</li>
</ol>
