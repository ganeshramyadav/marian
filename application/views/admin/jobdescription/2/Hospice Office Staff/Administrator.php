<p><strong><u>JOB DESCRIPTION</u></strong></p>
<h3>Administrator</h3>
<p>&nbsp;</p>
<p><strong>JOB SUMMARY:</strong></p>
<p>&nbsp;</p>
<p>The Administrator ensures quality and safe delivery of Hospice services; coordinates services that reflect Hospice's philosophy and standards of care; and plans, develops, implements and evaluates Hospice services, programs and activities.</p>
<p>&nbsp;</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>&nbsp;</p>
<ol>
<li>A person who is a licensed physician, <u>or</u></li>
<li>Is a registered nurse, <u>or</u></li>
<li>Has training and experience in health services administration and at least two (2) years of supervisory or administrative experience in Hospice or related health program.</li>
<li>Demonstrated ability in or application of organizational/communication skills.</li>
<li>Ability to deal effectively with high levels of stress.</li>
<li>Ability to enlist the cooperation of many people in furthering a program.</li>
</ol>
<p>&nbsp;</p>
<p><strong>RESPONSIBILITIES:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Organizes and directs Hospice&rsquo;s ongoing liaison among the Governing Body and staff.</li>
<li>Employees qualified personnel and ensures adequate staff education and evaluations.</li>
<li>Ensures the accuracy of public information materials and activities.</li>
<li>Implements an effective budgeting and accounting system; assures accuracy for billing procedures.</li>
<li>Shares copies of philosophy with all employees.</li>
<li>Consistently follows Hospice policies and procedures to set an example for employees.</li>
<li>Negotiates required contracts and ultimately oversees contract provisions.</li>
<li>Assesses employees on an ongoing basis to ascertain their understanding of policies and procedures.</li>
<li>Assists employees to support policies and achieve necessary changes.</li>
<li>Uniformly enforces policies and procedures.</li>
<li>Maintains two way communication with employees and fair administration of personnel policies.</li>
<li>Documents employee problems in personnel files.</li>
<li>Disciplines employees as necessary.</li>
<li>Directs Hospice's ongoing functions.</li>
<li>Monitors budget hours and does not exceed allowance each year.</li>
<li>Monitors equipment abuse and takes steps to keep it to a minimum.</li>
<li>Evaluates effectiveness and efficiency of Hospice.</li>
<li>Uses statistical data to determine quality and quantity of services.</li>
</ol>
<ol start="19">
<li>Maintains compliance with applicable federal, state and local rules and regulations and accreditation standards.</li>
<li>Supervises all business affairs.</li>
<li>Develops, implements and evaluates financial policies and procedures and records.</li>
<li>Develops, implements and evaluates budget plan and cost control policies and procedures.</li>
<li>Develops and implements salary program within approved policies and procedures.</li>
<li>Participates in personal professional growth and development.</li>
<li>Plans and directs operations to ensure the provision of adequate and appropriate care and services.</li>
<li>Fiscal planning, budgeting and management.</li>
<li>Recruits employees and retains qualified personnel to maintain appropriate staffing levels.</li>
<li>Establishes and maintains effective channels of communication.</li>
<li>Ensure personnel have current clinical information and current practices.</li>
<li>Evaluates services and programs.</li>
<li>Ensures staff development including orientation, inservice education and continuing education.</li>
<li>Coordinates with other program areas and management as appropriate.</li>
<li>Maintains current knowledge of local trends and issue.</li>
<li>Ensures that appropriate service policies are developed and implemented.</li>
<li>Directs staff in performance of their duties including admission, discharge and provision of service to patients.</li>
<li>Assures appropriate staff supervision during all operating hours.</li>
<li>Ensures the accuracy of public information materials and activities.</li>
<li>Establishes and oversees the Quality Assessments/Performance Improvement Program.</li>
<li>Appoints a similarly qualified alternate to be available at all times during operating hours in the absence of the Administrator.</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>WORKING ENVIRONMENT:</strong></p>
<p>&nbsp;</p>
<p>Works indoors in Hospice office.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>JOB RELATIONSHIPS:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Supervised by: Governing Body</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Workers Supervised: All Hospice staff and Contracted Services.</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
