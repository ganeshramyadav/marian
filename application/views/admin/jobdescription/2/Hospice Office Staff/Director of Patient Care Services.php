<p><strong>JOB DESCRIPTION</strong></p>
<p><strong>Director</strong></p>
<p>&nbsp;</p>
<p><strong>REPORTING RELATIONSHIP:</strong></p>
<p>&nbsp;</p>
<p>Supervised by: &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Administrator</p>
<p>Positions Supervised:&nbsp; Hospice Staff</p>
<p>Interrelationships:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Patients, family, IDG and other health care team members</p>
<p>&nbsp;</p>
<p><strong>JOB SUMMARY:</strong></p>
<p>&nbsp;</p>
<p>To provide Hospice care to the terminally ill hospice patient twenty-four hours/day, seven days/week. To work as a member of the Hospice team in providing Hospice care. Oversees and supervises all Hospice care to patients and families in all settings.</p>
<p>&nbsp;</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>&nbsp;</p>
<p>Educational/Degree:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Graduate from an accredited school of nursing</p>
<p>&nbsp;</p>
<p>Training/Licensure:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Currently licensed as a registered nurse to practice in the state.</p>
<p>&nbsp;</p>
<p>Knowledge/Skills/Ability:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ability to work independently, make accurate, and at times, quick judgments. Ability to supervise others appropriately. Ability to respond appropriately to crisis outside of a hospital setting. Acceptance of and adaptability to different social, racial, cultural and religious modes.</p>
<p>&nbsp;</p>
<p>Experience:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Minimum 2 years of experience as a registered nurse, preferred.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Active patient contact within past three years, preferred.</p>
<p>&nbsp;</p>
<p><strong>JOB FACTORS:</strong></p>
<p>&nbsp;</p>
<p><strong><u>Physical Requirements:</u></strong></p>
<p>Requires considerable physical effort most of the day including kneeling, squatting, reaching, twisting, climbing, walking, exposure to temperature and humidity changes and maximal assist in lifting and/or transferring of a 100 pound patient. Must possess sight/hearing senses or use appropriate adaptive devices that will enable senses to function at a level required to meet the essential duties of the position. Must provide evidence of annual TB test and other state-required tests or examinations.</p>
<p>&nbsp;</p>
<p><strong><u>Mental Requirements:</u></strong></p>
<p>Must be able to work independently, make judgments based on assessments and data available and act accordingly. Must be flexible, innovative and possess good interpersonal skills. Must be able to cope with mental and emotional stress and demonstrate emotional stability.</p>
<p>&nbsp;</p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>Working Conditions:</u></strong></p>
<p>Be able to tolerate exposure to elements including, but not limited to, odors, blood, body fluids and excrements, adverse environmental conditions and hazardous materials.</p>
<p><strong>&nbsp;</strong></p>
<p><strong><u>Transportation:</u></strong></p>
<p>Must have a current valid driver&rsquo;s license, auto liability insurance and reliable transportation.</p>
<p>&nbsp;</p>
<p><strong>Essential Functions:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Participates in developing standards which ensure safe and therapeutically effective service to patients and families. Has joint responsibility with the Administrator for seeing that standards are met.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Participates in developing objectives for Hospice.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Is responsible for seeing that objectives are implemented.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Consults with the Administrator to determine a staffing pattern which will accomplish stated objectives and promote maximum level of utilization of personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Is responsible for recruiting, hiring, evaluating and terminating service personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Participates in planning for the orientation of new employees. Conducts selected orientation classes.</li>
</ol>
<p><strong><em>&nbsp;</em></strong></p>
<ol start="7">
<li>Plans and arranges for consultation needs of staff; prepares and maintains current policies and procedures which meet Medicare, Medicaid, accreditation standards, state and federal laws, etc. and implements; revises concurrently.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Displays a willingness to support the policies and procedures and uses appropriate channels for change of such policies; establishes criteria and procedures for selection, promotion and termination of employment of service personnel.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Participates in establishing the functions and qualifications for each service position; coordinates interdisciplinary team services:</li>
</ol>
<ul>
<li>Assures ongoing assessment of patient/family needs and implementation of interdisciplinary group plan of care.</li>
<li>Assures physician approval of plans for continuity of medical care.</li>
<li>Provides individual or group support concerning job related stress or issues.</li>
</ul>
<p>&nbsp;</p>
<ol start="10">
<li>Organizes Hospice to delineate and delegate authority, functional responsibility, lines of relationship and communication to provide safe and therapeutically effective service.</li>
</ol>
<p>&nbsp;</p>
<ol start="11">
<li>Participates in coordinating Hospice's services with services of other community agencies.</li>
</ol>
<p>&nbsp;</p>
<ol start="12">
<li>Serves as a role model for all colleagues by setting an example of high standards in dress, conduct, cooperation and job performance.</li>
</ol>
<p>&nbsp;</p>
<ol start="13">
<li>Observes confidentiality and safeguards all patient related information.</li>
</ol>
<p>&nbsp;</p>
<ol start="14">
<li>Accepts responsibility for regular attendance and punctuality, fulfills job requirements without regard to time involved.</li>
</ol>
<p>&nbsp;</p>
<ol start="15">
<li>Serves as a resource person to employees.</li>
</ol>
<p>&nbsp;</p>
<ol start="16">
<li>Develops a cooperative relationship and communicates effectively and professionally with the physicians.</li>
</ol>
<p>&nbsp;</p>
<ol start="17">
<li>Investigates and reports any problem relating to patient care or conditions which might harm the patient and/or employee well-being.</li>
</ol>
<p>&nbsp;</p>
<ol start="18">
<li>Immediately reports any accident, incident, lost articles or unusual occurrence to the Administrator.</li>
</ol>
<p>&nbsp;</p>
<ol start="19">
<li>Attends pertinent continuing education programs other than routine inservices and shares information with staff.</li>
</ol>
<p>&nbsp;</p>
<ol start="20">
<li>Works with Administrator in identifying budgetary requirements and determining appropriate use of allocations.</li>
</ol>
<p>&nbsp;</p>
<ol start="21">
<li>Monitors equipment for appropriate use and take steps to keep misuse to a minimum.</li>
</ol>
<p>&nbsp;</p>
<ol start="22">
<li>Oversees Hospice's ongoing Quality Assessment/Performance Improvement (QAPI) program.</li>
</ol>
<p>&nbsp;</p>
<ol start="23">
<li>Provides 24 hour/day, seven (7) days/week on-call coverage.</li>
</ol>
<p>&nbsp;</p>
<ol start="24">
<li>Plans and supervises the Hospice.</li>
</ol>
<p>&nbsp;</p>
<ol start="25">
<li>Ensures that patients&rsquo; plans of care are developed, implemented and evaluated.</li>
</ol>
<p>&nbsp;</p>
<ol start="26">
<li>Conducts patient case conferences, inservices, staff meetings and maintains documentation; participates in community education projects.</li>
</ol>
<p>&nbsp;</p>
<ol start="27">
<li>Ensures that all necessary supplies and equipment are available.</li>
</ol>
<p>&nbsp;</p>
<ol start="28">
<li>Is normally available at all times during and after operating hours; may designate a qualified temporary replacement if he/she will not be available.</li>
</ol>
<p>&nbsp;</p>
<ol start="29">
<li>Ensures compliance with federal, state, and local rules, accreditation standards and Hospice policies in all patient care aspects of the Hospice.</li>
</ol>
<p>&nbsp;</p>
<ol start="30">
<li>Oversees staffing and assignments.</li>
</ol>
<p>&nbsp;</p>
<ol start="31">
<li>Addresses and corrects all patient concerns.</li>
</ol>
