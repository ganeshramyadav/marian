<p><strong><u>JOB DESCRIPTION</u></strong></p>
<h3>Quality Assessment/Performance Improvement Coordinator</h3>
<p>&nbsp;</p>
<p><strong>JOB SUMMARY:</strong></p>
<p>&nbsp;</p>
<p>A professional who assists the Director/Administrator with the development and ongoing administration of the Quality Assessment/Performance Improvement (QAPI) program.</p>
<p>&nbsp;</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Graduate of an approved school of professional nursing and currently licensed in the state(s) in which practicing; <u>or </u></li>
<li>A qualified health care professional.</li>
<li>Three to five (3-5) years of health care experience, preferred.</li>
<li>One (1) year experience in QAPI, preferred.</li>
<li>Ability to exercise initiative and independent judgment.</li>
<li>Ability to work with individuals, to enlist cooperation of many people to perform/achieve a common goal.</li>
</ol>
<p>&nbsp;</p>
<p><strong>RESPONSIBILITIES:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Understands and adheres to established Hospice policies and procedures.</li>
<li>Understands and promotes principles of continuous QAPI.</li>
<li>Responsible for the orientation of new staff to Hospice's QAPI program.</li>
<li>Assists in the planning and consultative needs of staff.</li>
<li>Assists in the preparation and implementation of policies and procedures which meet Medicare, Medicaid, accreditation standards and state and local laws.</li>
<li>Participates in studies and other administrative functions as assigned.</li>
<li>Serves as a role model for all colleagues by setting an example of high standards in dress, conduct, cooperation and job performance.</li>
<li>Observes confidentiality and safeguards all patient related information.</li>
<li>Accepts responsibility for regular attendance and punctuality; fulfills job related requirements without regard to time involved.</li>
<li>Serves as a resource person to employees.</li>
<li>Investigates and reports any problem relating to patient care and/or employee well-being.</li>
<li>Immediately reports any accident, incident, lost articles or unusual occurrence to the Director/Manager of Patient Services.</li>
<li>Attends pertinent continuing education programs other than routine inservices and shares information with staff.</li>
<li>Assists in the development of QAPI activities with appropriate data collection, aggregation, analysis, taking action and reporting of results according to Hospice's QAPI plan.</li>
<li>Reviews patient medical records for compliance with federal, state and local laws, accreditation standards and Hospice policies and guidelines.</li>
<li>Chairs QAPI committee; prepares QAPI reports/minutes of meetings; forwards reports according to QAPI plan.</li>
<li>Other duties as assigned by the Director/Administrator.</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>WORKING ENVIRONMENT:</strong></p>
<p>&nbsp;</p>
<p>Works indoors.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>JOB RELATIONSHIPS:</strong></p>
<p>&nbsp;</p>
<ol>
<li>Supervised by: Director/Administrator/Governing Body</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Workers Supervised: QAPI Committee</li>
</ol>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>RISK EXPOSURE:</strong></p>
<p>&nbsp;Low risk</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>LIFTING REQUIREMENTS:</strong></p>
<p>&nbsp;</p>
<p>Ability to perform the following tasks if necessary:</p>
<ul>
<li>Ability to participate in physical activity.</li>
<li>Ability to work for extended period of time while sitting or standing and being involved in physical activity.</li>
<li>Moderate lifting.</li>
<li>Ability to do extensive bending, lifting and standing on a regular basis.</li>
</ul>
