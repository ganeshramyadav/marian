<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Data Entry</em></p>
<p>&nbsp;</p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Information Systems Director</em></p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>Job Description Summary</strong></p>
<p>&nbsp;</p>
<p>The data entry/computer operator is responsible for the timely and accurate entry of a variety of data including patient information, human resource information, billing and payroll information.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>Essential Job Functions/Responsibilities </strong></p>
<p>&nbsp;</p>
<ol>
<li>Accurately enters a variety of data into information systems.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Assists the functions of billing, payroll, human resource management, and patient care management through timely and accurate data entry and system reporting.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Verifies accuracy of data entered through use of information systems reports.</li>
</ol>
<p>&nbsp;</p>
<ol start="4">
<li>Performs daily backup of information in the system(s).</li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li>Assists the Information Systems Director and management team members in the preparation of monthly management, statistical, and financial reports.</li>
</ol>
<p>&nbsp;</p>
<ol start="6">
<li>Alerts appropriate management team members regarding late or missing documents required for data entry.</li>
</ol>
<p>&nbsp;</p>
<ol start="7">
<li>Alerts appropriate members of the information system support staff to resolve system problems that negatively impact the speed and accuracy of data entry functions.</li>
</ol>
<p>&nbsp;</p>
<ol start="8">
<li>Performs other specific projects relating to data entry and computer operations as required.</li>
</ol>
<p>&nbsp;</p>
<ol start="9">
<li>Maintains confidentiality of patient and organization information at all times.</li>
</ol>
<p>&nbsp;</p>
<p>The above statements are only meant to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job-related tasks other than those stated in this description.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>Position Qualifications</strong></p>
<p>&nbsp;</p>
<ol>
<li>At least two years of experience in health care data entry, preferably in hospice care information systems.</li>
</ol>
<p>&nbsp;</p>
<ol start="2">
<li>Able to use IBM-compatible systems and equipment.</li>
</ol>
<p>&nbsp;</p>
<ol start="3">
<li>Is a high school graduate or equivalent, two years college preferred.</li>
</ol>
<p>&nbsp;</p>
