<p><strong>Job Title/Position:</strong>&nbsp;&nbsp;&nbsp; <em>Speech&ndash;Language Pathologist</em></p>
<p><strong>Reports To:</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>Clinical Supervisor</em></p>
<h3>Job Description Summary</h3>
<p>The speech&ndash;language pathologist is responsible for implementation of standards of care for speech language pathology services and for adherence to all conditions in the Service Agreement.</p>
<h3>Essential Job Functions/Responsibilities</h3>
<ol>
	<li>Conducts appropriate evaluations, assesses home environment (as appropriate), identifies equipment needs relative to speech/language function.</li>
	<li>Identifies functional speech defects and establishes a plan of care to improve patient&rsquo;s function.</li>
	<li>Administers speech therapy program utilizing specialized therapeutic technique and/or equipment.</li>
	<li>Recommends communication devices/aids as indicated.</li>
	<li>Confers with referring physician and other organization personnel to ensure coordinated and comprehensive care contributes to the total plan of care.</li>
	<li>As appropriate, participates in case conferences, inservices and other meetings required to ensure coordinated and comprehensive care.</li>
	<li>Prepares and submits notes per organization policy.</li>
	<li>Maintains clinical competency in speech language pathology practice and theory.</li>
	<li>Identifies patient and family/caregiver needs for other services and refers as appropriate.</li>
	<li>Instructs patient, family/caregiver and other Organization health care personnel in the patient&rsquo;s treatment regime as indicated.</li>
	<li>May supervise home health aide in procedures directly related to the speech&ndash;language therapy plan of care.</li>
	<li>Performs other duties as required to facilitate the delivery of speech language <br /> pathology services.</li>
</ol>
<p>The above statements are only meant to be a representative summary of the major duties and responsibilities performed by incumbents of this job.&nbsp; The incumbents may be requested to perform job related tasks other than those stated in this description.</p>
<p><strong>Job Title/Position:</strong>&nbsp; <em>Speech&ndash;Language Pathologist</em></p>
<h3>Position Qualifications</h3>
<ol>
	<li>Possesses a degree that includes an internship from an approved Master&rsquo;s program in Speech&ndash;Language Pathology, or as required by applicable law and/or regulation.</li>
	<li>Meets the education and experience requirements for a Certificate of Clinical Competence in Speech&ndash;Language Pathology, granted by the American Speech&ndash;Language&ndash;Hearing Association (ASHA).</li>
	<li>Is certified to practice Speech&ndash;Language Pathology.</li>
	<li>A minimum of one year's clinical experience in Speech&ndash;Language Pathology.</li>
	<li>Demonstrates good verbal and written communication, and organization skills.</li>
	<li>Possesses and maintains current CPR certification.</li>
</ol>
