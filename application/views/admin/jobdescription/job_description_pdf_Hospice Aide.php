<p><strong>JOB DESCRIPTION</strong></p>
<p><strong>Hospice Aide</strong></p>
<p><strong>REPORTING RELATIONSHIP:</strong></p>
<p>Supervised by:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Registered Nurse</p>
<p>Positions Supervised:&nbsp; N/A</p>
<p>Interrelationships:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Patients, family, IDG and other health care team members</p>
<p><strong>JOB SUMMARY:</strong></p>
<p>To provide personal care services to the terminally ill hospice patient as needed, under the direction of the RN. To provide assistance and understanding to the family and in time of bereavement. To work as a member of the Hospice team in providing Hospice care within the guidelines of the IDG care plan.</p>
<p><strong>QUALIFICATIONS:</strong></p>
<p>Educational/Degree:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; High school diploma or GED.</p>
<p>Training/Licensure:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Graduate from an approved training and competency course.</p>
<p>Knowledge/Skills/Ability:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Must meet federal Home Health Aide training and competency requirements. Ability to work independently, make accurate, and at times, quick judgments. Ability to respond appropriately to crisis outside of a hospital setting. Acceptance of and adaptability to different social, racial, cultural and religious modes.</p>
<p>Experience:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Minimum 2 years of experience as a nursing assistant, preferred.Active patient contact within past three years, preferred</p>
<p><strong>JOB FACTORS:</strong></p>
<p><strong><u>Physical Requirements:</u></strong></p>
<p>Requires considerable physical effort most of the day including kneeling, squatting, reaching, twisting, climbing, walking, exposure to temperature and humidity changes and maximal assist in lifting and/or transferring of a 100 pound patient. Must possess sight/hearing senses or use appropriate adaptive devices that will enable senses to function at a level required to meet the essential duties of the position. Must provide evidence of annual TB test and other state-required tests or examinations.</p>
<p><strong><u>Mental Requirements:</u></strong></p>
<p>Must be able to work independently, make judgments based on assessments and data available and act accordingly. Must be flexible, innovative and possess good interpersonal skills. Must be able to cope with mental and emotional stress and demonstrate emotional stability.</p>
<p><u></u><strong><u>Working Conditions:</u></strong></p>
<p>Be able to tolerate exposure to elements including, but not limited to, odors, blood, body fluids and excrements, adverse environmental conditions and hazardous materials.</p>
<p><strong><u>Transportation:</u></strong></p>
<p>Must have a current valid driver&rsquo;s license, auto liability insurance and reliable transportation.</p>
<p><strong>Essential Functions:</strong></p>
<ol>
	<li>Under the direction and ongoing supervision of the registered nurse, provides essential personal care assistance to the patient:</li>
	<ul>
		<li>Bath, care of mouth, skin and hair.</li>
		<li>Bathroom or in using bedpan or commode.</li>
		<li>Bed transfers as well as ambulation activities as ordered under the care plan.</li>
		<li>Range of motion exercises which the Aide has been taught by appropriate professional personnel.</li>
		<li>Performing such incidental household services as are essential to the patient&rsquo;s well- being at home.</li>
		<li>Preparing meals including special diet.</li>
		<li>Takes a patient&rsquo;s temperature, pulse, respirations and blood pressure if assigned by a registered nurse.</li>
		<li>Assists with shopping if the patient has no other resource available.</li>
	</ul>
	<li> Provides emotional support and compassionate care to the patient and family unit.</li>
	<ul>
		<li>Demonstrates listening skills.</li>
		<li>Offers encouragement when appropriate.</li>
		<li>Practices patience in voice and manner.</li>
	</ul>
	<li> Maintains required competency and skills.</li>
	<ul>
		<li>Documented initial and ongoing every 14 day supervision and evaluation by RN on each case assignment.</li>
		<li>Participates in annual performance review.</li>
	</ul>
	<li> Reports any changes in the patient&rsquo;s condition or family situation immediately by calling the Hospice office and notifying the RN.</li>
	<li> Practices Standard Precautions according to OSHA regulations and policy.</li>
	<li>Adheres to all policies and procedural guidelines.</li>
	<li> Attends at least twelve (12) hours of inservice education programs per year.</li>
	<li> Maintains current certification as required by the State.</li>
	<li> Maintains current knowledge and practice of infectious disease protocols.</li>
</ol>
<p>I have read the above job description and understand the duties and responsibilities associated with the position. I can perform the essential functions of this position without specific accommodations.</p>
