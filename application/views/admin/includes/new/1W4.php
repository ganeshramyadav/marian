					<p style=""><b>Purpose.</b> Complete Form W-4 so that your employer can withhold the correct federal income tax from your pay. Consider completing a new Form W-4 each year and when your personal or financial situation changes.</p>
					<p style=""><b>Exemption from withholding.</b> If you are exempt, complete only lines 1, 2, 3, 4, and 7 and sign the form to validate it. Your exemption for 2017 expires February 15, 2018. See Pub. 505, Tax Withholding and Estimated Tax.</p>
					<p style="">Note: If another person can claim you as a dependent on his or her tax return, you can’t claim exemption from withholding if your total income exceeds $1,050 and includes more than $350 of unearned income (for example, interest and dividends).</p>
					<p style="">Exceptions. An employee may be able to claim exemption from withholding even if the employee is a dependent, if the employee:</p><br />
					<ul>
						<li>Is age 65 or older,</li>
						<li>Is blind, or</li>
						<li>Will claim adjustments to income; tax credits; or itemized deductions, on his or her tax return.</li>
					</ul>
				</div>
			</div>
			
			<div class="col-md-4">
				<div style="font-size: 9pt;">
					<p>The exceptions don’t apply to supplemental wages greater than $1,000,000.</p>
					<p><b>Basic instructions.</b> If you aren’t exempt, complete the <b>Personal Allowances Worksheet</b> below. The worksheets on page 2 further adjust your withholding allowances based on itemized deductions, certain credits, adjustments to income, or two-earners/multiple jobs situations.</p>
					<p>Complete all worksheets that apply. However, you may claim fewer (or zero) allowances. For regular wages, withholding must be based on allowances you claimed and may not be a flat amount or percentage of wages.</p>
					<p><b>Head of household.</b> Generally, you can claim head of household filing status on your tax return only if you are unmarried and pay more than 50% of the costs of keeping up a home for yourself and your dependent(s) or other qualifying individuals. See Pub. 501, Exemptions, Standard Deduction, and Filing Information, for information.</p>
					<p><b>Tax credits.</b> You can take projected tax credits into account in figuring your allowable number of withholding allowances. Credits for child or dependent care expenses and the child tax credit may be claimed using the Personal Allowances Worksheet below. See Pub. 505 for information on converting your other credits into withholding allowances.</p>
				</div>
			</div>
			
			<div class="col-md-4">
				<div style="font-size: 9pt;">
					<p><b>Nonwage income.</b> If you have a large amount of nonwage income, such as interest or dividends, consider making estimated tax payments using Form 1040-ES, Estimated Tax for Individuals. Otherwise, you may owe additional tax. If you have pension or annuity income, see Pub. 505 to find out if you should adjust your withholding on Form W-4 or W-4P.</p>
					<p><b>Two earners or multiple jobs.</b> If you have a working spouse or more than one job, figure the total number of allowances you are entitled to claim on all jobs using worksheets from only one Form W-4. Your withholding usually will be most accurate when all allowances are claimed on the Form W-4 for the highest paying job and zero allowances are claimed on the others. See Pub. 505 for details.</p>
					<p><b>Nonresident alien.</b> If you are a nonresident alien, see Notice 1392, Supplemental Form W-4 Instructions for Nonresident Aliens, before completing this form.</p>
					<p><b>Check your withholding.</b> After your Form W-4 takes effect, use Pub. 505 to see how the amount you are having withheld compares to your projected total tax for 2017. See Pub. 505, especially if your earnings exceed $130,000 (Single) or $180,000 (Married).</p>
					<p><b>Future developments.</b> Information about any future developments affecting Form W-4 (such as legislation enacted after we release it) will be posted at www.irs.gov/w4.
